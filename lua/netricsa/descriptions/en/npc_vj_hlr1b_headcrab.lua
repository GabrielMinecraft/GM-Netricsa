PURPLE HEADCRAB FROM XEN


CLASS:      Headcrab, Purple
METABOLISM: Parasitic alien
PERCEPTION: Limited, host-driven instinct
SIZE:       ~1 ft (30 cm)
ENDURANCE:  Low
HOSTILITY:  High (aggressive, fast)
WEAPONS:    Bite (to infect host)
REWARD:     20 FC
THREAT:     Low individually, moderate in groups

DESCRIPTION:

The Purple Headcrab is a mutated Xen parasite. Unlike common headcrabs, it is slightly larger, faster, and more aggressive. Its primary purpose is to attach to a human host and transform them into a zombie.

TIPS:

- Fast, watch your movement - they jump unpredictably.
- Firearms or melee can stop them, but beware of swarms.