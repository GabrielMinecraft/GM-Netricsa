JUSTIN BARNS


CLASS:      Black Mesa Security Guard
METABOLISM: Human, Male
PERCEPTION: Standard visual + situational awareness
SIZE:       ~6 ft (183 cm)
ENDURANCE:  Medium
HOSTILITY:  Defensive / Allied
WEAPONS:    Glock 17
REWARD:     400 FC
THREAT:     Low

DESCRIPTION:

Justin Barns is a loyal Black Mesa security guard and one of Barney Calhoun's colleagues. He is armed with standard issue Black Mesa weapons and relies on squad tactics rather than heavy armor. During the Black Mesa incident, he proves himself as a brave ally, attempting to defend personnel and resist both HECU soldiers and alien forces.

While his training is limited compared to military operatives, Barns shows determination and loyalty. His lack of armor makes him more vulnerable, but he compensates with persistence and courage under pressure.

TIPS:

- Provide covering fire - he cannot withstand prolonged combat alone.
- Keep him close to  better support.
- Avoid open firefights; his armor is minimal.