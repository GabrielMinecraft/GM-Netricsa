COMBINE SLAM


WEAPON:      Combine SLAM (Combine Selectable Lightweight Attack Munition)
AMMO:        Remote-detonated explosive charges
ROF:         Manual placement, remote detonation

DESCRIPTION:

Tactical deployable explosive device capable of being attached to surfaces. Can be armed and detonated remotely, allowing ambushes, traps, or clearing of fortified positions. Used by Resistance and Combine forces alike.

NOTES:

- Allows strategic placement for area denial.
- Remote detonation ensures precise timing.
- Can stick to walls, floors, or vehicles.