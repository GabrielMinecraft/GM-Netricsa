THE STROGG EMPIRE


The Stroggs are a ruthless cybernetic race. Their culture is war; they are not born as normal beings.

Stroggification is a violent and merciless process that transforms captive humans and other species into a chimerical mixture of meat, implants, and aggression.

Their homeworld, Stroggos, is scorched by industrial factories and plants, with an atmosphere polluted to the extreme, where every being-soldier, medic, or artisan-participates in a vast war machine that produces copies of itself.

Their goal is to harvest organic components-tissue, organs, DNA-to serve as "raw material" in their cybernetic transformations and the realization of their vision of the ultimate form of biomechanical life. Their technology is ruthless, surgical, and unwavering; they will destroy any planet if they find what they need.