MKIV GRENADE LAUNCHER


WEAPON: MKIV Grenade Launcher
AMMO:   Grenades
ROF:    60-200 rounds/min depending on charge


DESCRIPTION:

The MKIV Grenade Launcher is the upgraded successor to the MKIII. Each trigger press fires a single grenade that either detonates on impact with an enemy or three seconds after firing. Grenades bounce off walls. Holding down the fire button charges the shot, increasing launch speed and distance; 


NOTES:

- Charged shots are useful for medium-to-long range engagements, travelling faster and further. 
- Grenades are dangerous if they miss, especially in tight areas - splash damage can hurt the user. 
- Very effective vs clusters of weak to medium enemies; avoid using charged shots in closed spaces.