ROCKET MISSILE LAUNCHER


WEAPON:      Rocket Missile Launcher
AMMO:        Technomagical Bolts
ROF:         ~90 missiles/min

DESCRIPTION:

A crude but ubiquitous armament among Mental's undead infantry, the Rocket Missile Launcher channels arcane energy into a volley of fizzing missiles. Mass-produced and nearly weightless, each unit harnesses a hidden reservoir of magical flux firing, albeit one bolt at a time.

NOTES:

- Low damage per shot, so not reliable for high-priority or armored targets.