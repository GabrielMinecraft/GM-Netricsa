THE MARTIAN EMPIRE OF R-A-C-E


The R-A-C-E Empire is an alien, ruthless race of aliens who have emerged from the depths of space and interdimensional portals to conquer Earth. Their technology permeates space: they use plasma weapons, flying saucers, and their own mothership-whatever it takes to impose their will on the strongest.

They're not simply invading-the R-A-C-E have been observing Earth for a long time: Black Mesa holds their technology, and the government has attempted to replicate some of their technology in secret research.