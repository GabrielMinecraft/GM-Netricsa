COMBINE REAGER


WEAPON:      Combine Reager
AMMO:        High-voltage energy cells
ROF:         ~120 shocks/min

DESCRIPTION:

Experimental electrical weapon issued to Combine engineers. Fires concentrated arcs of electricity that deliver a paralyzing shock to targets. Effective against both infantry and electronic systems, disabling enemies while causing severe damage through sustained discharge.

NOTES:

- Primary fire releases continuous electric arcs.
- Extremely effective against unarmored and biological targets.
- Can disable machinery, turrets, and electronic devices.
- Limited range; requires medium to close proximity.