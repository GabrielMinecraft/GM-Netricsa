THE CREATURES OF XEN


The Creatures of Xen are an empire of beings torn from their dimensions and fused together by an alien will. Once a Border World, Xen was a haven for those fleeing threats from their homeworlds.

At the head of Xen stands the Nihilanth, a gigantic psionic overlord who has gathered all sentient inhabitants, from Vortigaunts to Bullsquids, under his control. His power is maintained not by fear, but by mind control: Xen's beings sense each other's thoughts, living in a single energy node.