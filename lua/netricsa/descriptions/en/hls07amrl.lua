BLACK MESA, FREIGHT RAIL


Sam, the power is back online, and the cargo rail system is operational again. Against all odds, you've managed to secure yourself a ride out of that deathtrap. The good news: the train works. The bad news: everything else.

As soon as you climb aboard and hit the controls, the freight car jerks forward with a groan of rusty wheels. The track stretches deep into Black Mesa's subterranean maze, promising both escape and fresh nightmares.

Complications ahead:

H.E.C.U. patrols have fortified sections of the rail line. Expect ambushes at chokepoints, turrets by tunnels, and explosives planted on the tracks.

Alien activity is increasing-Xen fauna are phasing directly onto the tracks, either too stupid to move or smart enough to block your way.