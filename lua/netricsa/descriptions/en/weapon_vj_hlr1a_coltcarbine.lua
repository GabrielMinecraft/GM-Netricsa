COLT M4A1 CARBINE


WEAPON:      Colt M4A1 Carbine
AMMO:        5.56x45mm NATO rounds + 40mm grenades
ROF:         ~700 rounds/min

DESCRIPTION:

Standard-issue assault rifle for H.E.C.U. forces. Provides reliable automatic fire with good accuracy at medium range. Equipped with an underbarrel M203 grenade launcher, offering additional explosive firepower for tactical flexibility.

NOTES:

- Balanced weapon for most combat scenarios.
- Grenade launcher is effective against entrenched enemies.
- Loses accuracy at long range during sustained fire.