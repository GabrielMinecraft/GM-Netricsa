NAILGUN


WEAPON: Nailgun
AMMO:   Nails
ROF:    ~240 rounds/min

DESCRIPTION:

The Nailgun fires high-velocity nails  suited for close to mid-range combat. Each nail deals modest damage, but can inflict serious harm when hitting weak points or multiple projectiles land in sequence. It excels in tight spaces with manageable recoil and low spread.

NOTES:

- Good against light to medium targets, especially in indoor areas.
- Low per-shot damage requires accurate aim.
- Magazine upgrades raise tactical effectiveness in drawn fights.
- Nails can penetrate thin materials like wood or doors, but not heavy armor.
- Ask your enemies, "Would you like some fried nails?"