GLUON GUN


WEAPON:      Gluon Gun (Experimental Particle Accelerator)
AMMO:        Uranium-235 cells
ROF:         Continuous particle stream

DESCRIPTION:

Advanced experimental weapon projecting a constant energy beam that disintegrates targets on contact. Known for its distinctive appearance and high ammo consumption.

NOTES:

- Devastating sustained firepower.
- Consumes uranium cells rapidly.
- Restricted to Black Mesa advanced research staff.