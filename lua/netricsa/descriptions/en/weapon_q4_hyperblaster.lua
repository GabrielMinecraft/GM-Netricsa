HYPERBLASTER


WEAPON: Hyperblaster
AMMO:   Energy cells
ROF:    ~450 rounds/min

DESCRIPTION:

The Hyperblaster is a high-powered energy weapon that fires a continuous plasma when the trigger is held. It has a fast rate of fire and solid range. The plasma appears as a discharge of blue energy particles. Excellent for sustained damage and suppression.

NOTES:

- Uses energy cells instead of conventional ammunition.
- Continuous holding of the trigger allows uninterrupted fire.
- Effective both against single targets and area control.