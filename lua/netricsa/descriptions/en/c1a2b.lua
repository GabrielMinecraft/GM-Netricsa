BLACK MESA, CAFETERIA


Sam, welcome to Black Mesa's cafeteria-once a place for lukewarm coffee and questionable sandwiches, now the setting for a cosmic food fight. Unfortunately, the menu has changed: instead of lunch trays, you'll be dodging plasma fire, overturned tables, and the occasional airborne headcrab.

Scans confirm: both Xen creatures and Mental's troops are present here, and the cafeteria layout gives them plenty of cover. Expect firefights across tables, ambushes from the kitchen doorway.

Recommendation: use the chaos to your advantage. While Xen and Mental forces chew each other up, slip through the mess and keep heading toward the upper labs.