LIGHTNING GUN


WEAPON: Lightning Gun
AMMO: Energy charges
ROF: ~550 rounds/min


DESCRIPTION:

The Lightning Gun fires continuous arcs of lightning that pierce through multiple targets in a straight line, delivering sustained damage over time. Excellent for engaging enemies in long corridors or clustered formations. The beam appears as an electrical discharge connecting shooter and target.

NOTES:

- Capable of piercing multiple targets along a line of fire.
- Effectiveness diminished by obstacles and line-of-sight blockages.
- Energy consumption is high - wasted firing in empty space drains reserves.
- Best used from prepared positions with minimal lateral movement of enemies.