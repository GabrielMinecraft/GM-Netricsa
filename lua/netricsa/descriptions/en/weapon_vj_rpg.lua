RPG-7


WEAPON:      RPG-7 (Rocket-Propelled Grenade Launcher)
AMMO:        High-explosive rockets
ROF:         ~4 rockets/min

DESCRIPTION:

Portable anti-armor launcher capable of firing high-explosive rockets. Effective against vehicles, fortified positions, and groups of enemies. Simple aiming system allows use by both trained soldiers and insurgents.

NOTES:

- Massive explosive damage in target area.
- Limited ammo; each rocket must be aimed carefully.
- Backblast can be dangerous if fired in confined spaces.
- Reload required after each shot.