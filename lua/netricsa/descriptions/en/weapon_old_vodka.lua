ELDER'S VODKA


WEAPON:      Elder's Vodka
ROF:         One Bottle.

DESCRIPTION:

When consumed, vision blurs and the world distorts, yet hidden power flows through the drinker's veins. Strength manifests despite confusion and dizziness.

NOTES:

- Grants raw physical power.
- Severe distortion of perception.
- Dangerous in precise or tactical combat.
