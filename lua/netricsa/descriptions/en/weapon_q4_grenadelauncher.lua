GRENADE LAUNCHER


WEAPON: Grenade Launcher
AMMO:   Grenades
ROF:    ~20 grenades/min


DESCRIPTION:

The Grenade Launcher fires timed or impact grenades that detonate after being thrown. It gives strong area-of-effect damage, making it ideal to clear groups or destroy cover. With precise aiming, grenades can be lobbed over or around obstacles.

NOTES:

- Explosions can hurt the user if used too close.
- Effective against clusters and for breaking barriers.
- Use of bounce and angles is advantageous in fights.
- Be careful in confined areas because of splash damage.