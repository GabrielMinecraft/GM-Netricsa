BLACK MESA, SURFACE ESCAPE


Sam, against all odds, you've climbed out of the compactor - apparently, trash really does rise to the top. Your improvised escape route of crates and ventilation shafts has brought you outside. For the second time in hours... fresh air.

But don't celebrate yet. This isn't freedom. You've surfaced in a restricted industrial zone on the Black Mesa grounds. The layout suggests a processing plant - massive pipes, storage tanks, and heavy machinery dominate the skyline.

On the bright side? At least the view is better than being crushed into paste.