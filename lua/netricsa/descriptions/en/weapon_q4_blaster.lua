MARINE BLASTER


WEAPON: Blaster
AMMO:   None (infinite energy)
ROF:    ~360 rounds/min

DESCRIPTION:

The Blaster is the standard-issue energy pistol given to every Marine It fires small energy bursts with a simple trigger pull; holding down the trigger charges a stronger shot. A back-panel LED glows when fully charged. Also includes an under-barrel flashlight for lighting up dark areas.

NOTES:

- Charged shot deals much higher damage (around 50), but requires time to charge. 
- Does not use ammunition nor require reloading - infinite energy. 
- Integrated flashlight helps in dark sections without switching weapons.