M249 SAW


WEAPON:      M249 SAW (Squad Automatic Weapon)
AMMO:        5.56x45mm NATO belt-fed
ROF:         ~750 rounds/min

DESCRIPTION:

Heavy squad support weapon designed for sustained automatic fire. Belt-fed system provides large ammo capacity, making it ideal for suppressing enemy positions and cutting down groups of hostiles with overwhelming firepower.

NOTES:

- Extremely high rate of fire, effective for area suppression.
- Large ammo reserves allow prolonged engagements.
- Reduced mobility and heavy recoil during continuous fire.