GAUNTLET


WEAPON: Gauntlet
AMMO:   None (melee)
ROF:    ~150 slices/min 

DESCRIPTION:

The Gauntlet is a saw-like melee weapon.

On contact it slices the target and applies a knockback effect to push the enemy away.

NOTES:

- Deals 50 damage per slice. 
- Requires no ammunition - always available for use. 
- Best used with stealthy maneuvers, ambushes, or aggressive close combat. 
- With Quad Damage, a single slice can kill a full-health enemy.