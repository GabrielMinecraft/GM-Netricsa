SMG1


WEAPON:      SMG1
AMMO:        9x19mm rounds + secondary grenades
ROF:         ~750 rounds/min (primary), 1 grenade/sec (secondary)

DESCRIPTION:

Standard-issue automatic weapon. Fires rapid 9mm rounds for sustained suppression and includes an underbarrel grenade launcher for additional explosive support. Effective at close to medium range.

NOTES:

- High rate of fire, ideal for crowd suppression.
- Secondary grenades deliver area damage.
- Reduced accuracy at long distances.