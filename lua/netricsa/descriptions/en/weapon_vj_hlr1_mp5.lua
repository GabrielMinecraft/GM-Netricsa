MP5 SUBMACHINE GUN


WEAPON:      MP5 Submachine Gun
AMMO:        9x19mm rounds + 40mm grenades
ROF:         ~700 rounds/min

DESCRIPTION:

Versatile close-to-medium range automatic weapon. Provides sustained suppressive fire with reasonable accuracy. Integrated underbarrel grenade launcher increases tactical flexibility in varied combat situations.

NOTES:

- Effective against lightly armored infantry.
- Grenades are powerful in tight formations or confined spaces.
- Accuracy decreases significantly at long range.