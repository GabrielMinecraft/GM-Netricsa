BLACK MESA, UPLINK ACCESS


Sam, I hope you're ready, because this isn’t just another hallway full of soldiers. As soon as the elevator doors open, you'll find yourself staring at a heavily fortified H.E.C.U. encampment.

They've set up sandbags, mounted machine guns, and overlapping fields of fire. Clearly, they don't want anyone near that building. Unfortunately, that’s exactly where the uplink antenna is located.

Your task remains the same: reach the control room, adjust the array, and send the signal. Without that transmission, Lambda Complex stays sealed, and Black Mesa’s last chance of survival vanishes.