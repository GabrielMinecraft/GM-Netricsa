BLACK MESA, MISSILE SILOS


Sam, you've reached the rocket complex. This is what the scientist was talking about: the launch site where a satellite must be sent into orbit. Supposedly, this device will help the Lambda Sector stabilize the dimensional rift. Whether it's science, magic, or just desperate hope, one thing is clear - this launch is critical.

Unfortunately, you're not the only one who knows it. The H.E.C.U. forces have entrenched themselves in the silos, determined to prevent the launch. 

Recommendation: Sam, you don't need to understand the science - just make sure the rocket goes up. If the Lambda team is right, that satellite might give us the edge to shut this nightmare down. And if not... at least the fireworks will be spectacular.