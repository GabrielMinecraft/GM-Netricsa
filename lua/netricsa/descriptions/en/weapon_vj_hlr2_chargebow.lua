CHARGE BOW


WEAPON:      Charge Bow
AMMO:        Energy bolts
ROF:         ~20 bolts/min (primary), 1 volley/5 sec (alternate)

DESCRIPTION:

Experimental energy weapon developed by Resistance engineers. Fires single high-velocity energy bolts with precision. Alternate fire mode charges and releases a volley of three bolts simultaneously, increasing damage output for group targets or armored foes.

NOTES:

- Primary fire delivers precise, fast energy bolts.
- Alternate fire launches three charged bolts at once.
- Effective at medium to long range.
- Requires careful aim due to slow fire rate and charge time.