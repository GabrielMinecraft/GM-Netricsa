CITY, ZEROZEROZERO'S LAST MAP


The map city.bsp is considered a direct continuation of xmasxmas.bsp and fuck.bsp and one of the last known works of the mysterious mapper zerozerozero, who disappeared from the internet in 2013.

The file was found in an encrypted archive, the password to which turned out to be simple: "christmas". Upon opening, the player is presented with an empty city.

No windows. No doors. No life. Buildings stretch upward into the endless gray sky, like the unfinished scenery of someone else's dream.

