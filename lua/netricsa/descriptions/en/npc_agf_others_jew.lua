EWISH RESISTOR


CLASS:      Human Rebel
METABOLISM: Human, Male
PERCEPTION: Survivor's instinct
SIZE:       5.5-6 ft
ENDURANCE:  Medium
HOSTILITY:  Directed against German forces
WEAPONS:    none
REWARD:     250 FC
THREAT:     Low-Medium

DESCRIPTION:

A member of the oppressed population who rose against Germany's rule. Though not trained as a soldier, the Jewish Resistor relying on cunning, determination, and improvise. They embody resistance against overwhelming odds, driven not by power, but by the will to survive and oppose their enemies.

TIPS:

- Individually weak, but can be dangerous in groups.
- Their unpredictability makes them difficult to track.
- Lacks armor - precision fire is effective.