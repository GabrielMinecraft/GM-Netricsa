GRAVITY GUN


WEAPON:      Zero Point Energy Field Manipulator (Gravity Gun)
AMMO:        None (manipulates physics objects)

DESCRIPTION:

Experimental tool capable of picking up, holding, and launching objects with immense force. Can be used both for combat and puzzle-solving by manipulating the environment. Integral for controlling heavy or hazardous items at a safe distance.

NOTES:

- No traditional ammo; relies on physics objects in the environment.
- Can launch debris and enemies with lethal force.
- Essential for environmental interaction and creative problem-solving.
- Upgradable to increase power for launching larger objects.