DARK MATTER GUN


WEAPON: Dark Matter Gun
AMMO:   Dark Matter Slugs
ROF:    ~30 rounds/min

DESCRIPTION:

The Dark Matter Gun fires concentrated dark matter slugs that generate a miniature black hole upon impact. The resulting effect draws enemies inward and inflicts continuous damage. Ideal for crowd control and breaking through defensive positions.

NOTES:

- "Gravity pull" effect: nearby targets are sucked toward the impact point.
- Instability risk: powerful blasts may push the user back if used too close.
- Highly effective in corridors and enclosed spaces.
- Ammo is scarce-use each shot strategically.