FLAT GRASS


Flat Grass is one of the oldest and most iconic locations. It lives up to its name: a vast, flat field of grass surrounded by an endless blue sky.

There are no anomalies. Flat Grass is used to craft devices, test weapons, control vehicles, or simply wreak havoc without limits.

Key Features:

A sense of infinity: a vast, flat surface, perfect for large-scale construction and physics testing.

Minimalist architecture: just one building spawns, with one hidden room and a rooftop for observation.