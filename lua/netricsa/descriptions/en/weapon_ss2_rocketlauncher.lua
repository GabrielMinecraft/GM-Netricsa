XPML30 ROCKET LAUNCHER


WEAPON: XPML30 Rocket Launcher
AMMO:   75mm Hellion Rockets
RR:     100 RPM

DESCRIPTION:

The XPML30 is an upgraded version of the XPML21, using lighter 75mm Hellion rockets instead of 85mm Inferno rockets. It does not require reloading: a new rocket is automatically loaded with each pull of the trigger. The rockets fly in a straight trajectory and have high velocity, making them effective at medium and long ranges.

NOTES:

- Effective against armored enemies like the T-Mech due to its high rate of fire and powerful explosion.
- Less effective against airborne enemies at medium and long ranges due to the rocket's lower velocity.