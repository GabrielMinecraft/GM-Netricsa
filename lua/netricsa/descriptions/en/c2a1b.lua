BLACK MESA, ACID PIT ASCENT


Sam, in order to restore power to the cargo rail, you'll need to climb through one of the most unforgiving sections of the facility. The lower levels have been completely flooded with toxic waste, forming a glowing acid pit that eats through metal grates like paper. One misstep, and you'll dissolve faster than a sugar cube in hot coffee.

The only way forward is up-a maze of narrow catwalks, rusted pipes, and emergency ladders. Unfortunately, you're not climbing in peace. H.E.C.U. troopers have already entrenched themselves on the higher levels, using the vertical advantage to pin down anything moving below.

Hazards:

Acid pit beneath you: instant death if you fall.

Grenades and suppressing fire from soldiers above.