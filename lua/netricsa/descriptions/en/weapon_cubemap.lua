CUBEMAP


WEAPON:      Cubemap

DESCRIPTION:

Developer utility that spawns a set of reflective test spheres (cubemap probes) used to preview and capture environment reflections. When activated, the tool places several spheres with different material presets into the world so artists and level designers can check specular lighting, reflections and cubemap blending. Not a combat item - purely diagnostic and visual.

NOTES:

- Check specular lighting.