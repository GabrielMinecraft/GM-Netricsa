BLACK MESA DEVTEST, REACTOR


This test map throws the player straight into the reactor core chamber. Through a large observation window, the mysterious man with the briefcase can be seen quietly watching the chaos - standing beside a Vortigaunt, as if neither of them notice the ongoing battle.

Inside, things are far less calm. Houndeyes swarm the corridors.

And just when you think it's only Xen creatures... the military makes its move. H.E.C.U. soldiers are waiting in one of the room, ready to ambush and capture you.