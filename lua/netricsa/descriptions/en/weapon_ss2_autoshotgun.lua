AUTO SHOTGUN


WEAPON: Auto Shotgun
AMMO:   12-gauge shells (uses standard shells; max ammo ~200)
ROF:    Automatic - 60 RPM

DESCRIPTION:

Eight-barrel mechanically-indexing automatic shotgun. On each pull of the trigger one shell is fired and the mechanism instantly indexes to the next barrel, giving a faster effective firing behavior than a pump-action.

