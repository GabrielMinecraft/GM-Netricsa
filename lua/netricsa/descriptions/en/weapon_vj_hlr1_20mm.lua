20MM CANNON


WEAPON:      20mm Cannon (Mounted Autocannon)
AMMO:        20x102mm high-velocity shells
ROF:         ~450 rounds/min

DESCRIPTION:

Heavy stationary autocannon. Fires explosive 20mm shells at high velocity, shredding infantry and light armor with devastating efficiency. Designed for area denial and defensive suppression.

NOTES:

- Extreme firepower, highly effective against groups.
- Fixed emplacement limits mobility.
- Overheats with prolonged use, requiring short bursts.