ZAP GUN


WEAPON: Zap Gun 
AMMO:   Plasma from internal plasma micro-synthesizer
ROF:    ~120-240 

DESCRIPTION:

An energy weapon introduced.
Fires a projectile whose power depends on how long the trigger is held. Weak when lightly charged, very powerful at full charge. At full charge, the shot fires automatically once fully charged. A small amount of carbon in the plasma gives the projectile mild homing to organic targets and allows it to curve slightly around obstacles.

NOTES:

- Uncharged shots are weak and not very useful vs fast enemies.
- Fully charged shots are effective against weak to mid-tier enemies, particularly stationary ones.
- Good for hitting partially obscured targets or firing through cover.