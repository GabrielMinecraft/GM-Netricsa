THE RESISTANCE


The Resistance is a loose network of rebels, humans and Vortigaunts, united by a common goal: to liberate the Earth from the Combine's tyranny and reclaim their right to freedom and sanity. They do not constitute a unified army, but hundreds of small pockets operating in the shadows.

The Resistance is led by figures whose names evoke both hope and outrage: Eli Vance, Alyx Vance, Barney Calhoun, and others-seasoned leaders skilled in harnessing the power of science.

Their actions are divided: some scientists work on technologies that could break the Combine's tyranny, while others form armed units prepared to fight directly, regardless of losses.