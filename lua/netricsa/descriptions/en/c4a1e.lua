XEN, ASCENT TO THE PORTAL


You're almost there. At the very top of this abomination of a factory lies the portal that leads directly onto the path toward Nihilanth himself. The only way up? Organic elevators. Yes, you heard that right - the locals apparently got tired of normal buttons and cables, so they bred giant platforms that carry you upward when you stand on them.


But of course, Nihilanth won't just roll out the welcome mat. The portal spits out Controllers at regular intervals, each one eager to fry you with psychic blasts. Think of them as the universe's worst doormen.

Objective: survive the elevator ride, dodge the welcoming committee, and reach the portal. The other side is where Nihilanth's throne room begins - or ends, depending on how you perform.