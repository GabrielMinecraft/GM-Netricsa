MARINE SHOTGUN


WEAPON: Shotgun (Pump-Action Shotgun)
AMMO: Shells
ROF: ~48 rounds/min 

DESCRIPTION:

The Shotgun is a close-quarters pump-action weapon. Each trigger pull fires a spread of pellets, and the pump must be cycled before the next shot. Starts with an internal magazine of eight shells, with an upgrade later allowing a ten-shell magazine. Excellent in tight spaces and against enemies at close range.

NOTES:

- Damage can reach up to 140 if all pellets hit.
- Maximum reserve ammo is about 50 shells. 
- Ineffective at medium to long range because of pellet spread.
- Magazine upgrade improves reload efficiency and reduces downtime between shots.