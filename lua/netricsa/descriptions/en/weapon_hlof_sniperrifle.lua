M-40A1 SNIPER RIFLE


WEAPON:      M-40A1 Sniper Rifle
AMMO:        7.62x51mm NATO rounds
ROF:         Bolt-action, ~20 rds/min

DESCRIPTION:

Military-issue bolt-action sniper rifle chambered in 7.62mm NATO. Provides extreme accuracy at long distances, capable of eliminating enemies with a single well-placed shot. Equipped with a high-powered scope for precision targeting during the Black Mesa Incident.

NOTES:

- Very high damage; one-shot kill on most unarmored targets.
- Long-range accuracy makes it ideal for ambushes and stealth kills.
- Bolt-action requires rechambering after each shot, reducing fire rate.