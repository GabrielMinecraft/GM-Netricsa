DESCRIPTION:

Small Xen Mushrooms are a widespread flora of the border world Xen, often found in caves, damp tunnels, and areas saturated with organic decay. 

According to Xenobiological research, Xen Mushrooms share a distant evolutionary lineage with parasitic fauna such as Headcrabs. While far less mobile, they demonstrate similar sides.

