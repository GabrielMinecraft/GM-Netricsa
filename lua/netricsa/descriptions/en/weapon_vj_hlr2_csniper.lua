OVERWATCH SNIPER RIFLE MK2


WEAPON:      Overwatch Sniper Rifle MK2
AMMO:        Combine high-velocity rounds
ROF:         Bolt-action, ~20 rounds/min

DESCRIPTION:

Long-range precision rifle used by Combine snipers. Fires high-velocity rounds capable of penetrating armor and taking out targets at extreme distances. Equipped with a scope for enhanced accuracy. Designed for elimination of key targets with minimal exposure.

NOTES:

- Extremely accurate at long range.
- High damage per shot; can neutralize targets in a single hit.
- Bolt-action requires manual reload between shots.