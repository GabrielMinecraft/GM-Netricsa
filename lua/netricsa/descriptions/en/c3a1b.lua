BLACK MESA LAMBDA COMPLEX, THE FIREFIGHT


The elevator groaned its way down, and the moment the doors screeched open you were dropped into pure chaos. The H.E.C.U. soldiers are still fighting tooth and nail, their last squads dug in across shattered barricades.

The alien infantry has marched a heavy prize into the battlefield - a hulking ground-mounted laser cannon. Every shot carves through concrete like it's paper, cutting cover and men alike into ash.