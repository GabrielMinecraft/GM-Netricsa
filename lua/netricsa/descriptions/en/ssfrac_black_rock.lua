BLACK ROCK


Black Rock is a large research corporation in an alternate universe, similar to Black Mesa. It specializes in the study of alien artifacts and anomalous creatures, operating research vessels to conduct experiments.

The company has its own military force, the so-called "Blues", who guard facilities, escort operations, and ensure the safety of research vessels. These soldiers act as an elite military organization within Black Rock, ready to defend the company from any threats and control valuable materials.