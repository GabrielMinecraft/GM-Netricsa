BLACK MESA, UPPER HALLS


The elevator ride ends with a charming little scene: a terrified scientist banging on a locked door, begging the man with the briefcase inside to let him in. "They're coming, please, open the door!"

Spoiler: he doesn't. The briefcase man just stares, silent as ever. The scientist panics, runs... and immediately gets cut down by H.E.C.U. turrets.

And yes, that means the military is here now. Not to save anyone, of course - but to make sure nobody leaves alive.