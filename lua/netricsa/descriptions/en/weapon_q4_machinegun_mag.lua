MARINE MACHINE GUN


WEAPON: Machine Gun
AMMO:   Bullets
ROF:    ~600 rounds/min

DESCRIPTION:

The Machine Gun is a compact, select-fire automatic rifle used by the Space Marine Corps as their primary service rifle. It includes a flashlight under the barrel and a scoped mode for precision fire. The weapon can be fired in full auto from the hip or semi-auto when scoped, making it versatile for both close-quarters combat and mid-range encounters. 

NOTES:

- Scoped mode provides semi-automatic fire with improved accuracy. 
- Damage per shot: ~15 normally, ~20 when scoped. 
- Good against weak and medium enemies; heavy armored or boss targets require many bullets.
- The 80-round magazine upgrade greatly enhances staying power during prolonged fights.