KINGPIN FROM XEN


CLASS:      Xen, Psionic Entity
METABOLISM: unknown
PERCEPTION: psionic
SIZE:       12-15 ft
ENDURANCE:  high
HOSTILITY:  high
WEAPONS:    psionic blasts, mind control
REWARD:     4000 FC
THREAT:     high

DESCRIPTION:

The Kingpin is a massive, psionically powerful entity from Xen, designed for combat and control. Its body is dominated by an enormous brain, with four eyestalks evenly spaced around its head. It walking  the rear leg with a thick, worm-like tail. The Kingpin is to be found in Xen's caves, where it would use its psionic abilities to defend its territory.

TIPS:

- Due to its psionic nature, conventional weapons may be ineffective.
- Avoid close proximity; its mind control abilities can be overwhelming.