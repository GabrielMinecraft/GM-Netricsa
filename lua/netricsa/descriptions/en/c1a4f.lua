BLACK MESA, POWER CONTROL


Okay, Sam, you've found the Power Control sector-the second key system needed to activate the reactor purge. Without this, the ignition sequence in the silo will remain offline, and that oversized tentacle won't be going anywhere.

Access, however, is a problem. The only path forward takes you onto an old maintenance lift. And I use the term "lift" loosely-it looks like it was last serviced sometime before the invention of duct tape. Cables are frayed, the counterweight is misaligned, and every vibration makes it sway like a drunk sailor.

Hazards:

Barnacles have nested along the shaft walls, ready to drag you off the platform.


And of course, the ever-present risk that the lift simply gives up on life halfway down.