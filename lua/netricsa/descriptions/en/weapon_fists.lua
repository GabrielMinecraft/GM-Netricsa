FISTS


WEAPON:      Bare Fists
AMMO:        None (melee)
ROF:        ~90 strikes/min

DESCRIPTION:

Standard close-combat weapon. The hands deliver rapid strikes capable of staggering enemies and inflicting moderate damage. Ideal as a fallback when other weapons are unavailable.

NOTES:

- Unlimited use; no ammo required.
- Effective at close range only.
- Can combo with movement and environmental objects for added impact.
- Useful for conserving ammunition.