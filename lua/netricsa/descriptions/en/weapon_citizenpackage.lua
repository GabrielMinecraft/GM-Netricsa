CITIZEN PACKAGE


WEAPON:      Citizen Package

DESCRIPTION:

Non-combat item representing a cardboard package often seen in the hands of refugees and citizens. Cannot be used to attack or interact with enemies. Serves purely as a prop or symbolic tool of survival under the Combine regime.

NOTES:

- No combat functionality.
- Infinite use; cannot be depleted or reloaded.
- Symbolizes the burden carried by oppressed citizens.