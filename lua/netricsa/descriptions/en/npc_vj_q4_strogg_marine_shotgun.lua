STROGG SHOTGUN GUARD FROM STROGGOS


CLASS:      Strogg Guard Variant
METABOLISM: Cybernetic (Human-Strogg Hybrid)
PERCEPTION: Visual targeting + motion tracking
SIZE:       ~6 ft
ENDURANCE:  Medium-Low
HOSTILITY:  High
WEAPONS:    Built-in machine gun (right arm), sweeping bullet fire
REWARD:     500 FC
THREAT:     Medium

DESCRIPTION:

The Shotgun Guard is a lightly armored Strogg soldier equipped with a shotgun grafted directly onto its right arm. Designed for close-quarters combat, these units are among the most aggressive frontline troops encountered.

They rush enemies with reckless determination, firing bursts of buckshot at near point-blank range. Despite their simple tactics, a group of Shotgun Guards can overwhelm unprepared targets through sheer proximity and rate of fire.

TIPS:

- Keep your distance - their shotgun is lethal up close.
- Use explosives or automatic weapons to cut them down before they close in.