BLACK MESA: CANYON STRONGHOLD 


The ground opened into a canyon system - narrow ridges, unstable cliffs, and nowhere to hide. The perfect hunting ground for something airborne.

Sensors confirm it: a new Apache gunship has locked on. Not the one  before - fresh paint, fresh rockets, and a pilot eager to even the score. It circles above the canyon rims, using the walls as funnels to trap you in kill zones.