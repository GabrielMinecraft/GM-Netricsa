RPG V2


WEAPON:      RPG V2 (Rocket-Propelled Grenade Launcher)
AMMO:        High-explosive rockets
ROF:         ~4 rockets/min

DESCRIPTION:

Heavy anti-armor launcher used by the Resistance. Fires high-explosive rockets capable of obliterating vehicles, turrets, and groups of enemies. Equipped with a basic sight for aiming, making it effective at medium to long range.

NOTES:

- Massive explosive damage, ideal for armored targets.
- Limited ammo supply; rockets must be used carefully.
- Backblast and close-range use can be hazardous.
- Requires reload after each shot.