SNARK NEST, WEAPON


WEAPON:      Snark Nest, Weapon
AMMO:        Alien snark creatures
ROF:         ~1 per second (throwing speed)

DESCRIPTION:

Small aggressive alien organisms thrown as living weapons. Snarks attack nearby enemies relentlessly until they explode.

NOTES:

- Infinite aggression once released.
- Can harm user if no targets are present.
- Best used for area denial or distraction.