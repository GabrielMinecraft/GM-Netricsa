SHOCK ROACH


WEAPON:      Shock Roach (Biological Weapon)
AMMO:        Bio-electric charges (regenerating)
ROF:         ~120 shocks/min

DESCRIPTION:

Alien bio-organism captured and weaponized When held, it latches onto the user's arm and discharges streams of bio-electric energy at targets. Functions as a living weapon, constantly regenerating its own ammunition.

NOTES:

- Unlimited ammo due to natural regeneration.
- Primary fire: rapid electric bolts effective at medium range.
- Secondary fire: continuous stream of electricity for sustained damage.