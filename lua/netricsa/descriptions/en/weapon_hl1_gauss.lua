TAU CANNON


WEAPON:      Tau Cannon (Experimental Energy Weapon)
AMMO:        Uranium-235 cells
ROF:         Continuous beam or charged shot

DESCRIPTION:

Prototype particle accelerator firing high-velocity uranium particles. Can release rapid low-power beams or charge for a massive discharge capable of obliterating targets instantly.

NOTES:

- Charge mode delivers immense damage.
- Risk of self-harm if discharged too close.
- Effective against both organic and armored enemies.