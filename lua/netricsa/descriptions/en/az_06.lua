BLACK MESA, GARGANTUA SECTOR


The place reeks of smashed concrete and bad decisions. A full-grown Gargantua patrols the kill-zone - heavy, fast for its size. Meanwhile, in a window above, the man in the suit stands watching, briefcase at his side, utterly uninterested in the moral implications of the carnage. He's not here to help. He's here to observe. Probably to file notes.

Objective:

Survive the encounter and reach the extraction point - or, if the situation goes sideways, cut your losses and get out. Fast.