BLACK MESA, HE AGAIN



Sam, you've just entered the room with the giant ichthyosaur tank and room of the massive piston machinery pushing metal like it owns the place.

Something too calm. There, across the room, a man in a business suit-briefcase in hand-stands watching. He doesn't move, doesn't shout. Just observes. He, the ghost in the suit, appearing again.

He knows you're here. Probably expecting you. Or not...