ANNABELLE


WEAPON:      Annabelle Shotgun
AMMO:        357 Magnum rounds
ROF:         Pump-action, ~60 rds/min

DESCRIPTION:

High-powered shotgun capable of firing standard steel bullets. Designed for long-range precision strikes, Annabelle is deadly against both infantry and heavily armored targets. 

NOTES:

- Extremely high damage per shot.