ПИНГВИН С ГРАНАТОЙ


КЛАСС:        Биологическое оружие
МЕТАБОЛИЗМ:   хищная птица
ВОСПРИЯТИЕ:   острое зрение
РАЗМЕР:       60 см
ВЫНОСЛИВОСТЬ: низкая-средняя
ВРАЖДЕБНОСТЬ: высокая
ОРУЖИЕ:       привязанная граната, атака клеванием
НАГРАДА:      200
УГРОЗА:       средняя

ОПИСАНИЕ:

Этот, казалось бы, безобидный пингвин стал оружием. К его спине привязана граната, готовая взорваться при приближении к цели. Несмотря на небольшой размер, он удивительно быстр и агрессивен, бросаясь на врагов, используя смертоносную комбинацию клеваний и взрывов. Неосторожные столкновения могут оказаться фатальными, поскольку взрыв может нанести серьёзный урон как объектам, так и ближайшим союзникам.

СОВЕТЫ:

- Соблюдайте дистанцию; граната может взорваться при контакте.
- Используйте дистанционные атаки, чтобы нейтрализовать её до того, как она доберётся до вас.
- Избегайте скоплений - один пингвин может вызвать цепную реакцию.