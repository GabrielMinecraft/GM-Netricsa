СНАРК ГЕНМОДА-262 С UH-1


КЛАСС:      Ксенофауна
МЕТАБОЛИЗМ: Агрессивный плотоядный
ВОСПРИЯТИЕ: Острое (быстрые рефлексы, мгновенно обнаруживает угрозы)
РАЗМЕР:       ~1 фут
ВЫНОСЛИВОСТЬ:  Низкая (отдельно взятый экземпляр хрупок, но чрезвычайно трудно поразить)
ВРАЖДЕБНОСТЬ:  Экстремальная (атакует всё, что не является союзником)
ОРУЖИЕ:    Укус, когти, взрыв при смерти
НАГРАДА:     150
УГРОЗА:     Высокая (из-за скорости и количества)

ОПИСАНИЕ:

Эти снарки появляются непосредственно из Генмода-262 во вселенной UH-1. В отличие от обычных снарков, они исключительно быстры и непредсказуемы, что делает их чрезвычайно сложными для поражения или контроля. Они движутся хаотично, роем набрасываясь на цели и взрываясь при смерти, нанося дополнительный урон ближайшим врагам.

Несмотря на свои небольшие размеры, их агрессивность и большое количество делают их серьезной угрозой в любой схватке. Даже опытные бойцы с трудом справляются с ними, когда они появляются.

СОВЕТЫ:

- Не останавливайтесь - неподвижные цели быстро уничтожаются.
- Используйте оружие с зоной действия, чтобы противостоять роям.
- Избегайте ближнего боя; их взрывная смерть может быть смертельной в тесных помещениях.
- Нацельтесь на источник (Генмод-262), чтобы остановить дальнейшее появление.