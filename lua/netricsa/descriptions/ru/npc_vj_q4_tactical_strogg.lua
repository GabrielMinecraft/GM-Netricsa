ТАКТИЧЕСКИЙ СТРОГГ C СТРОГГОСА


КЛАСС:        Элитная пехота строггов
МЕТАБОЛИЗМ:   Кибернетический человеко-гибрид
ВОСПРИЯТИЕ:   Продвинутое (боевая подготовка + сенсорные имплантаты строггов)
РАЗМЕР:       ~1,8 м
ВЫНОСЛИВОСТЬ: Высокая (броня + кибернетические улучшения)
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Дробовик, пулемет, гипербластер, рельсовая пушка
НАГРАДА:      ~1200
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Тактический строгг - это строггифицированный пехотинец-человек, выбранный за исключительную боевую доблесть, тактический интеллект и безжалостную эффективность. Он не только обладает мощными кибернетическими аугментациями (броня, механические конечности, нейронные имплантаты), но и сохраняет значительную часть своей человеческой боевой интуиции.

Он действует в отрядах, используя укрытия, обходит противника с флангов и координирует свои атаки. В отличие от большинства отрядов Строггов, он использует человеческое оружие, а не встроенное.

СОВЕТЫ:

- Старайся уничтожать членов отряда с помощью рельсовых орудий или гипербластеров; они наносят огромный урон на расстоянии.
- Используй гранаты или взрывчатку, чтобы разрушить их строй.