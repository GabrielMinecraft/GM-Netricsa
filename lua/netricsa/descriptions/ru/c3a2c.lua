ЯДРО РЕАКТОРА ЧЁРНОЙ МЕЗЫ, ПОРТАЛЫ


Реакторный зал - больше не двигатель, а разлом. Порталы расцветают, словно злокачественные опухоли, потрескивающие дуги переплетающейся энергии. Учёные, до которых тебе нужно добраться, уже здесь, но прямых путей нет; только нестабильные порталы соединяют этот сектор с их.

И вот в чём проблема: шагнёшь не в тот, и тебя выплюнет в радиационную шахту, обрушающуюся шахту или, что ещё хуже, вообще никуда. Телеметрический анализ указывает на безопасную комбинацию: 2 → 1 → 7. Следуй этой последовательности, и ты пройдёшь лабиринт без ошибок.

Интересные места:

Нестабильность порталов. Если подождать слишком долго, то упустишь свой шанс - или, что ещё хуже, окажешься где-то случайно.

Наблюдатель. Рядом с одним из активных порталов стоит человек в деловом костюме. Всегда молчаливый, всегда наблюдающий. Он не вмешивается, не реагирует на угрозы, но его присутствие рядом не случайно. Это либо предупреждение... либо подтверждение.