НАПАЛМОМЁТ


ОРУЖИЕ:  Напалмовый Гранатомёт, Напалмомёт
ПАТРОНЫ: Напалмовые шары (огненные сферы)
СС:      ~30 шаров/мин

ОПИСАНИЕ:

Оружие, выпускающее огненные шары, которые загораются при соприкосновении с поверхностью. Создан для контроля пространства: коридоры, перекрёстки - идеальные места. Не слишком мощный против бронированных целей, но эффективно против слабых и средних врагов.

ПРИМЕЧАНИЯ:

- Напалм падает по баллистической траектории - нужно учитывать наведение. 
- Против бронированных врагов - слабое оружие. Лучше использовать где важно удержать территорию. 