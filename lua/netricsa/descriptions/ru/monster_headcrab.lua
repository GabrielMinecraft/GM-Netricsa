ОБЫЧНЫЙ ХЕДКРАБ С ЗЕНА


КЛАСС:      Хедкраб, обычный
МЕТАБОЛИЗМ: паразитическая ксенофауна
ВОСПРИЯТИЕ: чувствительный к вибрации, зависит от хозяина
РАЗМЕР:       ~1-2 фута
ВЫНОСЛИВОСТЬ:  средняя индивидуально
ВРАЖДЕБНОСТЬ:  высокая (при прикреплении)
ОРУЖИЕ: укус, постоянное паразитическое прикрепление
НАГРАДА: 150
УГРОЗА: умеренная индивидуально, высокая в группах

ОПИСАНИЕ:

Обычные хедкрабы - классические паразиты Ксена, известные тем, что они навсегда прикрепляются к голове хозяина. В отличие от земной разновидности, которая может спрыгивать и бродить самостоятельно, обычный хедкраб вгрызается в череп и остается прикрепленным на всю жизнь (или до тех пор, пока его не убьют), превращая хозяина в обычного зомби-хедкраба или бегущего зомби-хедкраба, который в конечном итоге эволюционирует в гонома.

Слияние ужасающе: череп хозяина виден под инопланетным панцирем, а контроль над существом абсолютен. Прикрепившись, хедкраб диктует движения, агрессию и поведение, превращая хозяина в медлительного, неумолимого хищника.

СОВЕТЫ:

- Избегайте укусов - после прикрепления удалить хедкраба практически невозможно, не убив хозяина.
- Огнестрельное оружие или взрывчатка эффективны одновременно против хозяина и паразита.
- Следите за группами; рои обычных хедкрабов могут быстро одолеть вторгшихся.