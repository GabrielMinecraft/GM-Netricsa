ОХОТНИК АЛЬЯНСА


КЛАСС:        Охотник-Синтент (Элитный отряд поддержки)
МЕТАБОЛИЗМ:   Биомеханический синтент (органика, слитая с механизмами)
ВОСПРИЯТИЕ:   Продвинутая оптика + отслеживание движения + интеллект стаи
РАЗМЕР:       8 футов (в плечах)
ВЫНОСЛИВОСТЬ: Высокая (быстрый и бронированный)
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Игломет, Таран, Клинки
НАГРАДА:      600
УГРОЗА:       Высокая

ОПИСАНИЕ:

Охотники - смертоносные синтенты среднего размера, разработанные Альянсом для борьбы с пехотой и техникой. Часто используемые стаями вместе со страйдерами, они служат застрельщиками, зачищают городские руины и с невероятной эффективностью охотятся на бойцов Сопротивления.

Хотя Охотники меньше страйдеров, они невероятно быстры и ловки, способны перепрыгивать через препятствия, вдавливать врагов в стены и прорываться сквозь укрытия. Их коронное оружие - дротик-метал, стреляющий светящимися дротиками, которые взрываются после попадания. На близкой дистанции они используют свои острые передние конечности, чтобы кромсать и пронзать добычу.

Охотники используют стайную тактику, общаясь с помощью жутких воплей и криков, координируя атаки и обходя врагов с флангов. Они неумолимы и бесстрашны, преследуя цели до полного уничтожения или победы. Ветераны Сопротивления считают их одними из самых опасных отрядов Альянса, за исключением Страйдера и Краба-Синтента.

СОВЕТЫ:

— Непрерывно двигайтесь - Охотники отлично загоняют неподвижные цели в угол.
— Их дротики ненадолго застревают перед взрывом - используйте этот момент, чтобы уклониться.
— Цельтесь в область головы/глаз для максимального урона.
— Используйте мощную огневую мощь (РПГ, дробовики, взрывчатку, AR2 Ball) - стрелковое оружие менее эффективно.