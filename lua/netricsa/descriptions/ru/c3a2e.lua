Последний порог перед Лямбдой охраняют не морпехи и даже не твари Ментала - это убийцы из Чёрные Оперативники.

Никаких разговоров, никаких насмешек, никаких лишних движений: лишь тени, мелькающие между стальными балками, и шёпот выстрелов из приглушённых пушек. Это не драка, это дуэль, и цена за малейшую ошибку - смерть.

Они знают, что вы идёте. Они расставили засады по всему подходу, используя высоту и тишину в своих интересах. Но как только они падут, путь будет свободен. Дальше больше не будет солдат. H.E.C.U. потерпели неудачу. Чёрные Оперативники нанесли свой удар. И теперь двери реактора стоят без охраны, ожидая, когда их взломают.

Отныне твои враги больше не будут носить форму. Поле битвы перемещается внутрь - в Зен, к порталам, к остаткам сил Ментала. Война человечества окончена. За пределами ждёт нечто более древнее, странное и гораздо более смертоносное.