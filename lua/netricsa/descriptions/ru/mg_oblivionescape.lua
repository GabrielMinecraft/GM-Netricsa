МЁРТВЫЕ ЗЕМЛИ ОБЛИВИОНА, ПОБЕГ ЧЕРЕЗ ВРАТА


Ну вот, как будто мало было инопланетных войск и злых солдат. Теперь мы оказались прямо в Мёртвых Землях Мерунеса Дагона. И если честно - по сравнению с этим местом даже самая убогая планета Клир кажется цветочками.

Ситуация простая: чтобы выбраться, нужно активировать Врата Обливиона. Для этого придётся собрать три Камня Душ, разбросанных вокруг громадной фигуры самого Дагона. Легко сказать... сложно выполнить.

По пути ожидают "приятные" мелочи: ловушки-шипы, которые явно охотятся на твои ноги (кеды), катящиеся валуны с идеальным таймингом, мерзкие Кланфиры, преследующие до потери пульса, и башни, выплёвывающие лаву, только чтобы напомнить - этот мир тебя ненавидит. Даже у Ментала было больше техники безопасности, чем здесь.

Итог: собираем камни, выживаем в этом парке садистских развлечений и активируем Врата. После всего этого даже толпа Клиров покажется дружеским объятием.