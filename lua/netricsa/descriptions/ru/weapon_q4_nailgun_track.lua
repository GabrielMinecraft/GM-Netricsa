ГВОЗДОМЁТ


ОРУЖИЕ:  Гвоздомёт
ПАТРОНЫ: Гвозди
СС:      ~240 выстрелов/мин

ОПИСАНИЕ:

Гвоздомёт - оружие ближней и средней дистанции, выпускающее высокоскоростные гвозди с сильной проникающей способностью. Каждая пуля-гвоздь наносит сравнительно скромный урон, но при попадании в слабозащищённые участки врага может вызвать большие повреждения. Отличается низким разбросом и хорошей скоростью стрельбы.

ПРИМЕЧАНИЯ:

- Эффективен против лёгких и средних целей, особенно в затянутых помещениях.
- Низкий урон на единичный выстрел; требует попадания в уязвимые места.
- При апгрейде магазина можно существенно увеличить мобильность в бою.
- Гвозди могут пробивать тонкие объекты (дерево, двери), но не толстую броню.
- Спроси у врагов "А жаренных Гвоздей не хочешь, нет?"