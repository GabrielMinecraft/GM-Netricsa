МАДЖЕНТОВЫЙ ВОРТИГОНТ


КЛАСС:        Вортигонтовый-мутант состоящий из Человеко-Чамтоуда
МЕТАБОЛИЗМ:   Бывший учёный-человек, слившийся с сущностью из Зена (Чамтоуда)
ВОСПРИЯТИЕ:   Псионическое восприятие, визуальное
РАЗМЕР:       ~6 футов (183 см)
ВЫНОСЛИВОСТЬ: Средне-высокая
ВРАЖДЕБНОСТЬ: Высокая (агрессивная)
ОРУЖИЕ:       Пурпурные энергетические заряды (псионические)
НАГРАДА:      600
УГРОЗА:       Высокая по отдельности, экстремальная в группах

ОПИСАНИЕ:

Маджентовые вортигонты - это извращённые варианты вортигонтов. Эти существа - бывшие учёные Чёрной Мезы, слившиеся с сущностями из Зена, что привело к появлению яркого пурпурного оттенка их кожи и снаряжения: пурпурные ошейники и нарукавники заменили типичный зелёный.

Их происхождение связано с тревожным экспериментом: учёный слился с Чамтоудом внутри водопроводной трубы, что привело к появлению первого Маджентового Раба. Этот процесс слияния повторялся для создания новых особей, что позволило им распространиться по всей Чёрной Мезе и стать всё более агрессивными и псионическими атакующими.

СОВЕТЫ:

- Атакуйте из укрытия: их энергетические взрывы псионические, быстрые и мощные.
- Изолируйте и уничтожайте: Группы невероятно опасны, и быстрая изоляция - ключ к успеху.
- Используйте опасности окружающей среды: загоняйте стаи в ловушки или узкие места, чтобы снизить их угрозу.