ЧЁРНАЯ МЕЗА, ОБРУШАЮЩАЯСЯ ТРУБА


Прогресс приводит тебя через то, что инженеры Чёрной Мезы великодушно назвали ремонтной трубой. На самом деле это ржавый цилиндр, едва выдерживающий собственный вес, не говоря уже о твоём.

Естественно, на полпути происходит неизбежное: труба стонет, гнётся и рушится. Ты проваливаешься в следующую камеру.

Снятие опасностей:

В зоне удара находится одинокий хедкраб, прячущийся под столом, по-видимому, ожидая идеальной засады.

Вместо этого сверху на него обрушивается весь вес Крутого Сэма.

Результат: один совсем плоский хедкраб и один совершенно растерянный Сэм, отряхивающийся.

Трубы больше нет, хедкраба больше нет, но твой путь открыт.

Просто помни: если инфраструктура здесь уже рушится под собственным весом, представь, как всё будет выглядеть, когда новые порталы продолжат разрывать реальность на части.