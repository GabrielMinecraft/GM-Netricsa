КОЛЬТ АНАКОНДА


ОРУЖИЕ:  Кольт Анаконда
ПАТРОНЫ: .44 Магнум
СС:      ~25-30 выстр/мин


ОПИСАНИЕ:

Кольт Анаконда - тяжёлый крупнокалиберный револьвер крупной рамной конструкции, разработанный для мощных охотничьих и служебных задач. Сочетает массивную стальную конструкцию и шестиствольный барабан, обеспечивая высокую останавливающую силу и стабильность выстрела. В игровых условиях служит как "плотный" оружейный вариант для ситуаций, где требуется максимум урона в одном выстреле.

ПРИМЕЧАНИЯ:

- Надёжен и неприхотлив в эксплуатации; стальная конструкция обеспечивает долговечность.
- Отлично сочетается с тактикой "один точный выстрел - перезарядка", либо как оружие "последнего аргумента".