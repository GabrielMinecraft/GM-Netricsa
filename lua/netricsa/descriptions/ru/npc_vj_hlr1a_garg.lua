ЧЕТЫРЕХНОГИЙ ГАРГАНТЮА С ЗЕНА


КЛАСС:      Прототитан Зена (подвид Гаргантюа)
МЕТАБОЛИЗМ: Биомеханический джаггернаут Зена
ВОСПРИЯТИЕ: Инфракрасное + сейсмическое восприятие
РАЗМЕР:       ~14 футов в высоту (более тяжелое телосложение, чем у классического Гаргантюа)
ВЫНОСЛИВОСТЬ:  Очень высокая (усиленная экзоскелетная броня с желтыми полосами)
ВРАЖДЕБНОСТЬ:  Экстремальная
ОРУЖИЕ:    Укус, удар по земле, грубая сила
НАГРАДА:     3000
УГРОЗА:     Катастрофическая в ближнем бою

ОПИСАНИЕ:

Четвероногий Гаргантюа - это отклоняющийся подвид, грубый гигант, созданный для физического доминирования, а не для разрушения. Его высокое тело покрыто толстым, похожим на камень панцирем с ярко-желтыми полосами, которые являются признаком упрочненного роста тканей, характерного для этого варианта.

В отличие от своего более известного собрата, четырехногий Гаргантюа не имеет когтей, извергающих огонь - вместо этого он полагается на сокрушающие кости укусы, разрушительные удары по земле и неумолимую силу в ближнем бою. Это делает его менее эффектным на расстоянии, но гораздо более смертоносным в ближнем бою, поскольку ни броня, ни баррикады не могут выдержать его сокрушительных ударов.

Учёные предполагают, что этот подвид был более ранней, альтернативной версией Гаргантюа, оптимизированной для выживания, а не для дальнобойной огневой мощи. Хотя он менее универсален, его прочность и атаки ударными волнами делают его не менее ужасающим.

СОВЕТЫ:

- Держитесь на расстоянии - этот вариант наиболее опасен в ближнем бою.
- Его укус может мгновенно убить небронированные цели.
- Ударные волны, наносимые по земле, могут пробивать укрытия — избегайте его линии атаки.
- Чтобы пробить его усиленную броню, требуются мощные взрывчатые вещества или сосредоточенный огонь.