Сэм, этот раздел идеально иллюстрирует фразу «неудачное время». Охранник заперт за запертыми воротами. К сожалению, он рассчитал время идеально, потому что прямо за ним стоит зомби, покачиваясь с тем самым взглядом, который безошибочно говорит: «Я пропустил обед».

Каковы шансы? Скажем так, пенсионный план охранника выглядит сильнее, чем его шансы на выживание. Ты можешь вмешаться.