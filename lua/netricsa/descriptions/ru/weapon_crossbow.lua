АРБАЛЕТ


ОРУЖИЕ:      Арбалет
БОЕПРИПАСЫ:        Стальные болты
ТЕМП ОГНЯ:         Болтовая затворная группа, ~15 болтов/мин.

ОПИСАНИЕ:

Точное оружие дальнего действия, оснащенное оптическим прицелом. Бесшумно стреляет стальными болтами, что делает его эффективным для уничтожения целей на большом расстоянии с высокой точностью.

ПРИМЕЧАНИЯ:

- Смертельно точное на большом расстоянии.
- Медленная перезарядка между выстрелами.
