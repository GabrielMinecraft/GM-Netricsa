ЗЕН, ПОДЪЁМ К ПОРТАЛУ


Ты почти у цели. На самом верху этой мерзкой фабрики находится портал, ведущий прямо на тропу к самому Нихиланту. Единственный путь наверх? Органические лифты. Да, ты не ослышался - местные жители, похоже, устали от обычных кнопок и кабелей, поэтому создали гигантские платформы, которые поднимают тебя наверх, когда ты на них встаёшь.

Но, конечно же, Нихилант не просто расстилает приветственный коврик. Портал регулярно извергает Контролёров, каждый из которых жаждет поджарить тебя психическими разрядами. Считай их худшими привратниками во вселенной.

Цель: пережить поездку на лифте, увернуться от приветственной комиссии и добраться до портала. С другой стороны начинается - или заканчивается - тронный зал Нихиланта, в зависимости от твоих успехов.