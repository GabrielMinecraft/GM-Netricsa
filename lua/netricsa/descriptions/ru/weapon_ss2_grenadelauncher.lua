ГРАНАТОМЁТ МКIV


ОРУЖИЕ:  Гранатомёт MKIV
ПАТРОНЫ: Гранаты
СС:      От 60 до 200 выстрелов/мин в зависимости от степени зарядки

ОПИСАНИЕ:

Гранатомёт MKIV - усовершенствованная версия MKIII. Каждый выстрел выпускает одну гранату, которая взрывается при контакте с врагом или через ~3 секунды после выстрела. Гранаты отскакивают от стен. При удержании курка граната пускается с большей скоростью и дальностью, но урон остаётся тем же - 135 очков (35 прямого попадания + 100 урона взрывом).

ПРИМЕЧАНИЯ:

- Заряженные выстрелы полезны на средних и дальних дистанциях; они летят дальше и быстрее.
- Гранаты снаряда могут быть опасны для игрока, если промахнуться - особенно в замкнутых пространствах. 
- Эффективен против групп слабых и средних врагов; не стоит использовать заряженные гранаты в тесных помещениях - риск взрыва близко к себе.