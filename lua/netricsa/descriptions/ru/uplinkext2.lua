ЧЁРНАЯ МЕЗА, ДОСТУП К СВЯЗИ


Надеюсь, ты готов, потому что это не просто очередной коридор, полный солдат. Как только двери лифта откроются, ты обнаружишь перед собой хорошо укреплённый лагерь H.E.C.U.

Они установили мешки с песком, установили пулемёты и перекрывающиеся сектора огня. Очевидно, они не хотят никого видеть рядом с этим зданием. К сожалению, именно там находится антенна связи.

Твоя задача остаётся прежней: добраться до пункта управления, настроить антенну и послать сигнал. Без этой передачи комплекс "Лямбда" останется запечатанным, и последний шанс Чёрной Мезы на выживание исчезнет.