РЕАКТИВНАЯ УСТАНОВКА


ОРУЖИЕ:  Реактивная установка
ПАТРОНЫ: Ракеты
СС:      ~40 выстрелов/мин


ОПИСАНИЕ:

Классическая реактивная установка. Выпускает мощные ракеты, которые взрываются при достижении цели или по прошествии времени, нанося урон области. Каждая ракета обладает сильным импульсом, способным отталкивать врагов и разрушать слабые конструкции. Отличный выбор для расчистки зон и борьбы с группами врагов.

ПРИМЕЧАНИЯ:

- Взрывы наносят значительный урон в зоне и могут травмировать стрелка, если выстрелите слишком близко.
- Эффективен против групп и столкновений; слаб против быстрых мелких целей.
- Важно сохранять запас ракет и использовать точное прицеливание — промахи дорого обходятся.