ЧЁРНАЯ МЕЗА, ВОЗВРАЩЕНИЕ В ОФИСЫ


Это офисы, но не те, что ты видел раньше. Немного другая планировка, тот же застоявшийся запах кофе. Чёрной Мезе очень понравились её кабинки.

К сожалению, ты не единственный посетитель. Бойцы Ментала уже заняли часть этого сектора, превратив офисный лабиринт в поле боя из перевёрнутых столов и осколков стекла. Ожидай серьёзного сопротивления в узких коридорах.

И вот он снова: мужчина с портфелем. Он замечен спокойно идущим по одному из коридоров, не тронутым хаосом, словно всё было запланировано.