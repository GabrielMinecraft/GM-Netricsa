РЕАКТОР ЛЯМБДА ЧЁРНОЙ МЕЗЫ, ПРЕДУПРЕЖДЕНИЕ УЧЁНОГО


Прежде чем ты ушёл, учёный объяснил, что единственный путь вперёд - через затопленную шахту доступа реактора. Необходимо активировать насосы, чтобы поднять уровень воды и дать тебе достаточно воды, чтобы заплыть внутрь. Здесь нет солдат - не по счастливой случайности, а потому, что это единственный оставшийся открытый вход. Все остальные точки доступа перекрыты. Они ждали тебя.

Директива:

Активировать насосы.

Поднять уровень воды.

Заплыть внутрь реактора.

Отсюда солдат нет. То, что ждёт впереди, ещё хуже.