ГРАНАТОМЁТ MKIII


ОРУЖИЕ:    Гранатомёт MKIII
ПАТРОНЫ:   разрывные снаряды особой мощности калибра 40 мм.
СС:        переменная скорострельность

ОПИСАНИЕ:

Надёжный пехотный гранатомёт. Конструктивно приспособлен для ведения огня на различные дистанции. MKIII предназначен для метания особо мощных гранат через препятствия, а также из-за угла, что снижает шансы стрелка быть раненым осколками. Встроенный контактный детонатор срабатывает только при попадании в живую цель, от стен и других препятствий граната отскакивает.

ПРИМЕЧАНИЯ:

- Короткое нажатие на спусковой крючок - и граната полетит недалеко. Удерживай крючок подольше - и граната полетит дальше.
- Разрывные снаряды особой мощности чрезвычайно эффективны против больших скоплений врагов.
- Гранату можно послать рикошетом за угол или перебросить через стену.