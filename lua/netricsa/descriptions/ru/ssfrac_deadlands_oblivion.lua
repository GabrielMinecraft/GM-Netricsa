ДАЭДРА МЁРТВЫХ ЗЕМЕЛЬ ОБЛИВИОНА


Даэдра Мёртвых Земель Обливиона - это армия разрушения, рожденная в пламени владений Мерунеса Дагона, Принца Хаоса и Разрушения. Они воплощения ярости своего владыки, существа, созданные из пепла, стали и ненависти.

Каждый воин Мёртвых Земель носит в сердце пламя своего Принца. Они не знают страха, не ведают жалости и не различают добро и зло. Для них существует лишь порядок разрушения - священный цикл конца и возрождения.

Когда врата Обливиона раскрываются, их армии извергаются в мир смертных, словно лавовый поток - сжигающий всё на своём пути. Они не подчиняются времени и не боятся смерти, ведь для них конец - это начало нового огня.