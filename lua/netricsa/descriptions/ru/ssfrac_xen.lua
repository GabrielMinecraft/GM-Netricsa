СУЩЕСТВА ЗЕНА


Существа Зена - это империя существ, вырванных из своих измерений и спаянных в единое целое чуждой волей. Когда-то Погранчный мир - Зен был пристанищем для тех, кто бежал от угроз из родных миров.

Во главе Зена стоит Нихилант - гигантский псионический властитель, собравший под своим контролем всех разумных обитателей, от вортигонтов до буллсквидов. Его власть держится не страхом, а подчинением через разум: существа Зена ощущают мысли друг друга, живя в едином энергетическом узле.