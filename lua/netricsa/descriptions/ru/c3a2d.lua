ЯДРО РЕАКТОРА ЧЁРНОЙ МЕЗЫ, ЗАПУСК ЛЯМБДЫ


Вот оно. Последняя тренировочная площадка перед Зеном. Учёные собрались на верхней платформе, нервно следя за показаниями устройства, которого здесь быть не должно - Временного Замка, созданного путём реверс-инжиниринга сирианского Ключа Вечности и кое-как вмонтированного в реакторную решётку Чёрной Мезы. Как только он стабилизируется, он откроет управляемый мост в Зеном.

Путь к ним до неприличия прост: уничтожь трёх хедкрабов, поднимиcь по лестнице, и команда встретит тебя. Но не позволяй этому обмануть тебя - настоящее испытание начинается с началом обороны.

Параметры миссии:

Защити ведущего учёного. Он единственный, кто обучен управлять модифицированным Ключом к Вечности. Если он погибнет, врата никогда не стабилизируются.

Ожидай серьёзного вторжения. Как силы Ментала, так и существа Зена чувствуют разлом и бросят всё, чтобы остановить активацию.

Удерживай до стабилизации. Устройство требует непрерывного потока энергии для синхронизации; прерывание его работы в середине цикла может вызвать катастрофический резонанс.

Осложнения:

Знакомые лица. Джастин Барнс, Адриан Шепард и сам Гордон Фримен были замечены направляющимися сюда. Настоящий Фримен - не ты, не эта путаница. Но они опаздывают, и вы уже проделали тяжелую работу. Если они вообще прибудут, то не успеют помочь в этой битве.

Ограниченная поддержка. Учёные могут предоставить тебе только один инструмент: прототип реактивного ранца, предназначенного для длительных прыжков. Он понадобится, как только портал стабилизируется. Берегите его как золото.