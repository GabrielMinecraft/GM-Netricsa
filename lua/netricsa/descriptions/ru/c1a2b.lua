ЧЁРНАЯ МЕЗА, КАФЕТЕРИЙ


Добро пожаловать в кафетерий Чёрной Мезы - когда-то заведение, где можно было выпить тёплого кофе и полакомиться сэндвичами сомнительного качества, а теперь - место космической битвы за еду.

К сожалению, меню изменилось: вместо подносов с обедом тебе придётся уворачиваться от плазменного огня, перевёрнутых столов и, порой, от летающих хедкрабов.

Сканирование подтверждает: здесь присутствуют как существа Зена, так и войска Ментала, а планировка кафетерия даёт им достаточно укрытий. Ожидайте перестрелок за столами и засад у входа в кухню.

Рекомендация: используй хаос себе на пользу. Пока силы Зена и Ментала перемалывают друг друга, проскользни сквозь хаос и продолжай движение к верхним лабораториям.