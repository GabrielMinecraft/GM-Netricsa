СТРОЙКА, НАБЛЮДЕНИЕ


На первый взгляд, это просто стройка. Пустые стены. Открытое небо. Знакомые очертания. Но что-то... не так.

Датчики регистрируют низкоэнергетическую аномалию в одном из тёмных помещений техобслуживания. Ни звука, ни движения, но тепловые сигнатуры мерцают - как будто кто-то стоит совершенно неподвижно в тени. Наблюдает.

Каждый раз, когда ты поворачиваешься спиной, тебе кажется, что воздух меняется. В дверном проёме становится немного темнее. Возможно, там стоит какая-то фигура или, может быть, это просто твой разум пытается сшить образ из помех.

Рекомендация: продолжай двигаться. Не задерживайся в тёмных коридорах. И если ты чувствуешь, что за тобой кто-то наблюдает, ты, вероятно, прав.