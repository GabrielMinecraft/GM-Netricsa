ЛУЧЕВОЕ РУЖЬЁ XL4-P "GHOSTBUSTER"


ОРУЖИЕ:    Лучевое ружьё XL4-P
ПАТРОНЫ:   элементы питания X7
СС:        продолжительная
ВЕС:       9 кг.

ОПИСАНИЕ:

Являясь более экспериментальным ответвлением линейки оригинального лазерного ружья XL2, это невероятно редкое оружие стреляет смертельным непрерывным лучом, способным прожечь любую поверхность. Во время проведения секретных полевых испытаний, в которых участвовало лишь несколько наиболее опытных бойцов АОЗ, данное оружие быстро получило прозвище "Ghostbuster" по аналогии со схожим лучевым оружием из одноимённого фильма.

Лучемёт использует те же элементы питания, что и лазерное ружьё XL2, и изначально разрабатывался в качестве альтернативы громоздкому и тяжёлому минигану, однако он так никогда и не поступил в серийное производство, став объектом солдатских баек и легенд о таинственном "супероружии".

ПРИМЕЧАНИЯ:

- Используй его для уничтожения больших скоплений средних врагов.
