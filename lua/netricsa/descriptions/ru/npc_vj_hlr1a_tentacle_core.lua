ЯДРО ТЕНТАКЛЕЙ С ЗЕНА


КЛАСС:      Тентаклевое ядро
МЕТАБОЛИЗМ: ксенобиологический нейронный кластер
ВОСПРИЯТИЕ: управляет щупальцами с помощью вибрации и псионических сигналов
РАЗМЕР:       5-10 футов 
ВЫНОСЛИВОСТЬ:  средняя
ВРАЖДЕБНОСТЬ:  пассивная (косвенная)
ОРУЖИЕ:    нет (координирует щупальца)
НАГРАДА:     2500
УГРОЗА:     катастрофическая (пока щупальца активны)

ОПИСАНИЕ:

Эта изолированная масса - ядро Тентаклей, центральный блок управления всеми щупальцами в своей области. Без ядра Тентакли бездействуют, оставляя окружающую среду относительно безопасной. Его внешний вид напоминает гротескный мозг с сенсорными узлами, идеально предназначенными для передачи сигналов каждому щупальцу. Хотя само по себе оно беззащитно, пока оно остается неповрежденным, каждое подключенный к нему тентакль жив и смертельно опасен.

СОВЕТЫ:

- Сосредоточьте огонь или опасные факторы окружающей среды непосредственно на ядре.
- После его уничтожения все активные щупальца становятся бесполезными.
- Подходите осторожно: даже если оно выглядит беспомощным.