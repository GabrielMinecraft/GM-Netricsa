ОПИСАНИЕ:

Альфа Сержант H.E.C.U. (специалист по ближнему бою) - это жестокий эксперт ближнего боя, применяемый для подавления сопротивления в ограниченном пространстве или при отсутствии подавляющего огня. Усиленная броня позволяет им выдерживать атаки, сокращая дистанцию, а смертоносные навыки ближнего боя делают их ходячими палачами.

В отличие от обычных морских пехотинцев, эти сержанты обучены обходить укрытия, бросаться на противника и выводить из строя несколько целей, используя чистую силу и точные удары. Они часто сопровождают отряды регулярных подразделений H.E.C.U., выступая в роли ударных отрядов для прорыва вражеских рубежей.

СОВЕТЫ:

- Соблюдайте дистанцию ​​- их удары в ближнем бою сокрушительны.
