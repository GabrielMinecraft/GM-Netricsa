РАКЕТОМЁТ XPML30


ОРУЖИЕ:  Ракетомёт XPML30
ПАТРОНЫ: 75 мм ракеты Hellion
СС:      100 выстрелов/мин

ОПИСАНИЕ:

XPML30 - усовершенствованная версия XPML21, использующая более лёгкие 75 мм ракеты Hellion вместо 85 мм Inferno. Не требует перезарядки: при каждом нажатии на спусковой крючок автоматически загружается новая ракета. Ракеты летят по прямой траектории и обладают высокой скоростью, что делает их эффективными на средних и дальних дистанциях.

ПРИМЕЧАНИЯ:

- Эффективен против бронированных врагов, таких как T-Mech, благодаря высокой скорости стрельбы и мощному взрыву.
- Менее эффективен против летающих врагов на средних и дальних дистанциях из-за недостаточной скорости ракеты.