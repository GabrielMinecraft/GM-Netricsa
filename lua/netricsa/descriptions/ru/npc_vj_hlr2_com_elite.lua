ЭЛИТНЫЙ СОЛДАТ OVERWATCH АЛЬЯНСА


КЛАСС:        Трансчеловеческий пехотинец (элитный отряд)
МЕТАБОЛИЗМ:   Аугментированный человек, мужчина
ВОСПРИЯТИЕ:   Тактический HUD + инфракрасные/тепловые системы
РАЗМЕР:       180-200 см
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Импульсная винтовка AR2, Энергетические шары
НАГРАДА:      900
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Элитный солдат Overwatch Альянса - это высший наземный отряд, развёрнутый Альянсом на Земле. Узнаваемые по ярко-белой броне и одному зловеще светящемуся красному глазу, они служат как карателями, так и ударными отрядами в крупномасштабных операциях. Их продвинутые синтетические имплантаты обеспечивают им превосходные рефлексы, выносливость и бдительность по сравнению со стандартными солдатами Дозора.

Элитные подразделения используются только тогда, когда активность сопротивления выходит за пределы возможностей обычных подразделений. Их боятся за их жестокую эффективность, активное использование импульсной винтовки AR2 и скоординированную тактику, часто сочетающую подавляющий огонь с энергетическими шарами, чтобы выбить противника из укрытия.

Их броня усилена против огня стрелкового оружия, что делает их крайне живучими противниками. Их присутствие часто сигнализирует о том, что Альянс перешёл к полномасштабному уничтожению или подавлению, не оставляя повстанцам возможности отступать.

СОВЕТЫ:

- Огонь импульсной винтовки сокрушителен - используйте укрытие и постоянно двигайтесь.
- Взрывчатка и тяжёлое вооружение - самый эффективный способ их уничтожить.
- Они часто действуют отрядами - никогда не думайте, что Элитный действовал в одиночку.
- Красный визор может выдать их позицию в условиях низкой освещённости.