ЧЁРНАЯ МЕЗА, ПОКАЗАНИЯ ВЫЖИВШЕГО


Отлично, ты связался с одним из выживших учёных. И да, он упорно называет тебя Фрименом. Я пыталась его поправить, но он, похоже, убеждён, что чувак в кроссовках Adida - это просто «новый лабораторный образ».

Вот что он нам рассказал: чтобы остановить эту катастрофу, тебе нужно добраться до комплекса «Лямбда» в дальнем конце Чёрной Мезы. По его словам, именно там перегруппировываются последние оставшиеся исследователи, и у них может быть план по закрытию межпространственного разлома.

Он также клянётся, что всем в Лямбде можно доверять - никаких секретов, никаких двойных агентов, никаких сюрпризов. Что, учитывая твой день, либо утешает... либо вызывает подозрения.

Осложнение: основная транспортная система Чёрной Мезы вышла из строя. Трамваи, лифты, даже половина грузовых лифтов, вышли из строя. Ваш единственный жизнеспособный маршрут - железнодорожная грузовая ветка за складом.

Но есть одна загвоздка: чтобы добраться туда, вам придётся пересечь сектор испытаний ракет, а каждый туннель, ведущий в этом направлении, кишит врагами.

Итак, план прост (и под простотой я подразумеваю граничащий с безумием): угнать грузовой поезд, пробраться через ракетные шахты и надеяться, что это приведёт вас к тому, что осталось от команды «Лямбда».

Рекомендация: прими слова учёного за чистую монету: «Лямбда» - твой лучший шанс положить конец этому кошмару. Но будь готов, Сэм: доверие - это хорошо, а огневая мощь - лучше.