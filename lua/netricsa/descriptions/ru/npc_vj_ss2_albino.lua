ЦИКЛОП-АЛЬБИНОС С МАГНОРА


КЛАСС: Орда     Ментала, Модифицированный дикий червь
МЕТАБОЛИЗМ:     Плотоядный
ВОСПРИЯТИЕ:     Нацеливание самонаводящейся сферой + ближний бой
РАЗМЕР:         ~22 фута
ВЫНОСЛИВОСТЬ:   Высокая
ВРАЖДЕБНОСТЬ:   Чрезвычайная
ОРУЖИЕ:         Самонаводящаяся энергетическая сфера, удар рукой
НАГРАДА:        800
УГРОЗА:         Высокая

ОПИСАНИЕ:

Первоначально гигантские черви с планеты Магнор, циклопы-альбиносы были ужасающе преобразованы с помощью рук и ног, полученных от Ментала в ходе извращённого генетического эксперимента. Их тела не совсем усвоили замысел, поэтому они остаются ужасно большими и странными по пропорциям. Они вселяют страх в сердца даже самых смелых двумя коронными атаками:

Сферический взрыв: выпускает самонаводящуюся энергетическую сферу, которая захватывает игроков - крайне важны манёвры уклонения.

Удар рукой: Если вы настолько глупы, что подошли слишком близко, будьте готовы к тому, что вас ударит сокрушительная сила.

СОВЕТЫ:

- Большой размер делает их лёгкой мишенью для ракетниц и гранатомётов.
- Используйте оружие, такое как «Клюдовик», ракеты или тяжёлое огнестрельное оружие, для быстрых убийств.
- Точный выстрел из пушки SBC может уничтожить их на месте, если повезёт.