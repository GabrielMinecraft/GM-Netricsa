ЧЁРНАЯ МЕЗА, ЭХО СЕКТОРА


И снова человек с портфелем здесь - молчаливый, неприкасаемый, наблюдающий с мостика наверху. Он либо твой самый преданный поклонник... либо твой худший рекрутер.

Этот сектор кажется пугающе знакомым: коридоры, лаборатории, оборудование - как будто Чёрная Меза скопировала и вставила собственные чертежи, чтобы сэкономить время и деньги. Но не расслабляйся слишком сильно. Некоторые комнаты отличаются. Несовпадающие углы. Оборудование, которого ты раньше не видел. Новые ловушки в старом лабиринте.

Представь это как дежавю... только с чуть более высоким шансом смерти.

И есть проблема: чтобы добраться до офисов - и вам, и тому "другому Сэму" - необходимо восстановить электроснабжение. Где-то в этом лабиринте спрятан рычаг, управляющий электричеством сектора. Найди его, иначе вы оба застрянете.

Ещё одна сложность: по территории рыщет Пантерай. Он пожирает всех.