ВЕРТОЛЁТ-ОХОТНИК АЛЬЯНСА


КЛАСС:        Тяжёлый ударный вертолёт
МЕТАБОЛИЗМ:   Механический / Продвинутый винтокрыл (питается от энергетических систем Альянса)
ВОСПРИЯТИЕ:   Инфракрасное + Отслеживание движения + Визуальные датчики
РАЗМЕР:       Очень большой (размером с вертолёт; больше игрока, доминирует с воздуха)
ВЫНОСЛИВОСТЬ: Очень высокая (тяжелая броня, устойчивость к стрелковому оружию)
ВРАЖДЕБНОСТЬ: Экстремальная (против пехоты, против техники, агрессивно преследует цели)
ОРУЖИЕ:       Импульсная пушка, сбрасываемые бомбы/мины, управляемые ракеты (в некоторых боях)
НАГРАДА:      5000
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Вертолёт-Охотник - грозный винтокрыл Альянса, используемый для оказания мощной огневой поддержки, патрулирования зон и охоты на особо важные цели, такие как беженцы или отряды Сопротивления. Управляемый двумя солдатами Overwatch (пилотом и стрелком), он сочетает в себе человеческий дизайн и технологии Альянса, создавая воздушного хищника.

У него есть передняя импульсная пушка для стрельбы прямой наводкой, крылья, оснащённые ракетами, и он может сбрасывать бомбы с таймером и мины. Он оснащён мощными сенсорами и системами слежения (включая инфракрасные), чтобы следить за врагами через укрытия. Его кабина, сделанная из пуленепробиваемого и непрозрачного стекла, защищает экипаж, затрудняя прямое прицеливание.

СОВЕТЫ:

- Огонь из стрелкового оружия практически бесполезен, пока у вас нет более тяжёлого оружия или позиции.
- Всегда двигайтесь; оставаясь на открытом пространстве, вы попадёте под удар импульсной пушки или бомб.
- Следите за сбросом бомб/мин - они смертоносны, особенно под техникой или в узких проходах.
- Используйте укрытия или возвышенности, чтобы уклониться от линии огня.
- По возможности стреляйте снизу или из укрытия, когда кабина уязвима.