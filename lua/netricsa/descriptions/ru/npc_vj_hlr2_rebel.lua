ПОВСТАНЕЦ СОПРОТИВЛЕНИЯ


КЛАСС:        Боец Сопротивления
МЕТАБОЛИЗМ:   Люди
ВОСПРИЯТИЕ:   Среднее (обучены основам боя и тактики)
РАЗМЕР:       ~1,7-1,8 м
ВЫНОСЛИВОСТЬ: Средняя (лёгкая броня или импровизированная защита)
ВРАЖДЕБНОСТЬ: Высокая (к войскам Альянса)
ОРУЖИЕ:       Пистолет, пистолет-пулемёт, дробовик, гранаты
НАГРАДА:      200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Повстанцы - члены человеческого Сопротивления, организованного для борьбы с оккупацией Альянса. Это обычные люди, быстро обучаемые владению огнестрельным оружием и координации атак, часто импровизирующие тактику в городских и сельских зонах боевых действий.

Они действуют в отрядах, прикрывают мирных жителей, обеспечивают безопасность путей снабжения и оказывают критически важную поддержку основным оперативникам, таким как Гордон Фримен или Аликс Вэнс. Хотя они менее продвинуты, чем войска Альянса, их приспособляемость, моральный дух и численное превосходство делают их грозными противниками.

Повстанцы полагаются на командную работу и тактику «бей и беги», а не на грубую силу. Их стойкость проистекает из преданности выживанию человечества, а не из превосходного вооружения. Их часто можно увидеть взаимодействующими с техникой, такой как танк «Меркава», или помогающими учёным и медикам на поле боя.

СОВЕТЫ:

- Защищайте их - они наиболее эффективны в группах.
- Снабжайте их боеприпасами и аптечками для поддержания давления на передовой.
- Используйте их для разведки местности и засад на патрули Альянса.
- Отряды повстанцев могут сокрушить даже легкобронированные подразделения Альянса при условии их координации.