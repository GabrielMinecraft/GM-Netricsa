ПЁС


КЛАСС:        Робот-компаньон / Боевая машина Сопротивления
МЕТАБОЛИЗМ:   Механический (питается от экспериментального энергетического ядра)
ВОСПРИЯТИЕ:   Улучшенные сенсоры + визуальное распознавание
РАЗМЕР:       ~8-10 футов
ВЫНОСЛИВОСТЬ: Очень высокая (тяжелая броня, прочная конструкция)
ВРАЖДЕБНОСТЬ: Низкая (защитная, агрессивна только к угрозам)
ОРУЖИЕ:       Чистая сила, Снаряды из металлолома, Импровизированное оружие
НАГРАДА:      1000
УГРОЗА:       Высокая (при провокации)

ОПИСАНИЕ:

Пёс — огромный, преданный робот, созданный Эли Вэнсом и постоянно совершенствуемый его дочерью Аликс. Изначально задуманный как механический «питомец» и телохранитель, Пёс превратился в мощный источник силы и выносливости. Несмотря на свой устрашающий вид, он обладает игривым, почти детским характером - он любит играть в апорт, с лёгкостью бросая огромные предметы.

Однако в бою Пёс превращается в машину разрушения: разрывает на части силы Альянса, метает машины в пух и прах или пробивает укреплённые заграждения. Его уникальное сочетание грубой силы и отточенной точности делает его незаменимым для Сопротивления.

Преданность Пса Аликс безусловна. Он защищает её любой ценой, часто бросаясь в безвыходные ситуации с неудержимой энергией.

СОВЕТЫ:

- Пёс может поднимать и бросать предметы тяжелее любого человека - используйте его силу, чтобы расчищать себе путь.
- Устойчив к большинству видов стрелкового оружия; лучше всего позволить ему принять на себя огонь противника.
- Когда Пёс впадает в ярость, держитесь подальше - будь то друг или враг, его энергия способна сокрушить всё на своём пути.