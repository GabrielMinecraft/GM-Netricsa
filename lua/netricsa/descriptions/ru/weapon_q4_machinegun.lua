МАШИНГАН МОРПЕХОВ


ОРУЖИЕ:  Машинган
ПАТРОНЫ: Патроны (пули)
СС:      ~600 выстрелов/мин

ОПИСАНИЕ:

Стандартная автоматическая винтовка, базовое оружие пехоты Космического корпуса. Универсальное и надёжное оружие, оснащено фонариком под стволом и возможностью прицельного огня со снайперским режимом. Позволяет вести огонь сразу или точечно при прицеле, что делает его пригодным как для ближнего боя, так и для средних дистанций. 

ПРИМЕЧАНИЯ:

- Прицел позволяет вести полуавтоматический огонь с большей точностью. 
- Урон: стандартный выстрел наносит ~15, во время прицеливания - ~20. 
- Эффективен против слабых и средних врагов; для тяжёлых целей требуется много пуль. 
- Магазин на 80 патронов после апгрейда даёт преимущество в длительных перестрелках.