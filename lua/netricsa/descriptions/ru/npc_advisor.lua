МЕТАБОЛИЗМ: Паразитический псионический организм (поддерживается биотехнологической системой жизнеобеспечения)
ВОСПРИЯТИЕ: Телепатическое + псионическое сенсорное поле
РАЗМЕР:       ~12 футов (в парящем состоянии)
ВЫНОСЛИВОСТЬ:  Очень высокая (защищен полетом + бронированной кожей)
ВРАЖДЕБНОСТЬ:  Экстремальная (психическое доминирование)
ОРУЖИЕ:    Псионическое доминирование, телекинетический захват, щупальце-хоботок, левитационный удар
НАГРАДА:     2200
УГРОЗА:     Катастрофическая

ОПИСАНИЕ:

Советники Альянса - настоящие надзиратели оккупации Земли, которых редко видят рядовые солдаты, но которых глубоко боятся даже среди Overwatch. Эти гротескные существа, похожие на слизней, парят в поле псионической энергии, их тела раздуты и поддерживаются инопланетными технологиями.

Они командуют силами Альянса через телепатические сети, проецируя свою волю непосредственно на трансчеловеческих солдат и порабощенных людей. В бою они используют ужасающие псионические силы - останавливают врагов на полном ходу, сокрушают их невидимой силой или швыряют об стены. Их питательный хоботок способен пронзать черепа и извлекать нервную ткань, что используется как для питания, так и для допросов.

Хотя советники редко появляются на поле боя, их появление сигнализирует об абсолютном господстве Альянса в данном районе. Их присутствие означает, что ни одно действие не останется незамеченным и сопротивление не продлится долго.

