ЧЁРНАЯ МЕЗА, ДО ИНЦИДЕНТА


Телепортация заканчивается... но не там, где ты ожидал. Ни разрушенного Зена, ни последствий. Вместо этого ты стоишь в Чёрной Мезе, до катастрофы. Лаборатории целы, учёные спокойны, и всё пахнет прокисшим кофе и озоном.

И снова тебя принимают за Фримена. Я бы пожаловалась, но, похоже, в каждой временной линии людям нравится это имя. Где-то в этом огромном комплексе другой "Сэм, который ты, но не ты" готовится активировать Антимасс-спектрометр. Мы оба знаем, чем это обернётся.

Тем временем вас втягивают в другой проект. Исследователи шепчутся о параллельном испытании - прототипе портала, независимом от основного эксперимента. Они хотят, чтобы вы наблюдали за ним, пока Спектрометр пробуждается к жизни в другом крыле.

Два эксперимента. Две катастрофы, готовые столкнуться. Одна история уже была. Другая... вот-вот будет написана.

С другой стороны, это означает, что тебя обвинят только в половине апокалипсиса. Прогресс!