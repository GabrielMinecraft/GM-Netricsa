ЧЁРНАЯ МЕЗА, ЛИФТ


Этот сектор - настоящий логистический кошмар. Единственный путь вперёд - через грузовой лифт. Звучит просто, но, конечно же, это не так. Спускаясь, ты сможешь с удовольствием уворачиваться от падающих сверху хедкрабов - видимо, гравитация - их излюбленная тактика засад.

Внизу всё становится ещё лучше: шахта лифта напрямую соединяется с мусороуплотнителем. Один неверный шаг, и тебя превратят в крутые фрикадельки. Время будет критически важным, чтобы ты мог безопасно перебраться.

Ваша цель - добраться до моста за этим участком. К сожалению, датчики подтверждают, что он уже обрушился. Другими словами, лёгкого пути нет. Тебе придётся идти крюком, углубляясь вглубь комплекса, пока не найдёшь пассажирский лифт, ведущий к офисным зданиям.

Рекомендация: следи за падающими паразитами и держись подальше от вращающихся лопастей. Если ты переживёшь и то, и другое, офисные работники наверху будут в восторге от такого... колоритного гостя.