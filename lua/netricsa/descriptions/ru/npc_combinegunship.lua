ШТУРМОВИК АЛЬЯНСА


КЛАСС:        Воздушный штурмовой синтент
МЕТАБОЛИЗМ:   Синтетический / Биомеханический (слияние органики и машины)
ВОСПРИЯТИЕ:   Продвинутые визуальные датчики + датчики отслеживания движения
РАЗМЕР:       ~40-45 футов
ВЫНОСЛИВОСТЬ: Очень высокая (крепкая броня и устойчивость к стрелковому оружию)
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Тяжёлая импульсная пушка (спереди), вентральная варп-пушка (внизу)
НАГРАДА:      5000
УГРОЗА:       Очень высокая

ОПИСАНИЕ:

Штурмовик Альянса - летающий штурмовой синтент, используемый Альянсом. Он напоминает странный гибрид органической и механической конструкции с округлым корпусом, двумя крыльями, похожими на ласты, в передней части, большим ротором «хвостом» и механическими компонентами (открытыми трубками, светящимися деталями), которые выдают его синтетическую природу.

Он патрулирует небо над критически важными территориями Альянса (Цитадель, Нова Проспект, ключевые аванпосты), обрушивая точный огонь из импульсной пушки и иногда более мощных залпов из нижней пушки (хотя нижняя пушка в основном не используется, за исключением особых команд). Обладая высокой маневренностью, способной разворачиваться на 180° в полёте, он избегает ближнего боя и сражается на расстоянии, используя свою скорость и ловкость, чтобы оставаться над угрозой.

СОВЕТЫ:

- Используйте взрывное оружие (РПГ, гранаты) вместо стрелкового - он практически неуязвим для пуль.
- Двигайтесь и используйте укрытия. Они предпочитают дальнобойные атаки и преимущество в высоте.
- Обратите внимание на предупреждающий звук перед началом стрельбы - это даёт вам краткий шанс спрятаться.
- Не выходите на открытые пространства, особенно под траекторией полёта.
- Старайтесь атаковать из слепых зон (снизу, сзади или сбоку), чтобы Штурмовик не сбил ракету.