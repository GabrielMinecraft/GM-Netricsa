ОПИСАНИЕ:

Кингпин - массивное, обладающее псионической силой существо c Зена, созданное для боя и контроля. Его тело занимает огромный мозг с четырьмя стебельчатыми глазами, равномерно расположенными вокруг головы. Он передвигается на задней ноге с толстым червеобразным хвостом. Кингпина можно найти в пещерах Зена, где он использует свои псионические способности для защиты своей территории.

СОВЕТЫ:

- Из-за его псионической природы обычное оружие может быть неэффективным.
- Избегайте близкого расстояния; его способность контролировать разум может быть подавляющей.