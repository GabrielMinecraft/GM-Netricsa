БАРНАКЛ, ЗЕМНАЯ РАЗНОВИДНОСТЬ


КЛАСС:      Барнакл, Земная разновидность
МЕТАБОЛИЗМ: плотоядный хищник, охотящийся из засады
ВОСПРИЯТИЕ: чувствительность к вибрации + контактная чувствительность
РАЗМЕР:       6-8 футов (длина языка до 20 футов)
ВЫНОСЛИВОСТЬ:  средняя
ВРАЖДЕБНОСТЬ:  пассивная, охотится из засады
ОРУЖИЕ:    липкий язык, мощные челюсти
НАГРАДА:     150
УГРОЗА:     средняя (высокая в ограниченных пространствах)

ОПИСАНИЕ:

Барнакл - неподвижный хищник, обитающий в Зене, часто встречается висящим с потолков, в пещерах и на промышленных сооружениях. Он вытягивает длинный липкий язык, который свисает к земле, ожидая, когда что-нибудь коснется его. Как только контакт установлен, жертва подтягивается вверх к пасти с острыми зубами, где она раздавливается и пожирается целиком. Несмотря на неподвижность и предсказуемость, Барнаклы смертельно опасны для неосторожных, особенно в узких коридорах или когда жертвы отвлечены другими угрозами.

СОВЕТЫ:

- Смотрите вверх - Барнаклы легче всего заметить на расстоянии.
- Используйте их против врагов: заманивайте зомби или Комбайнов в их языки.
- Короткая, контролируемая стрельба быстро их убьёт.