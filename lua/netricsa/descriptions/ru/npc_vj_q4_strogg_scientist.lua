НАГРАДА:      700
УГРОЗА:       Средняя

ОПИСАНИЕ:

Ученый - это подразделение строггов, занимающееся медицинскими исследованиями и процессом строггификации. Его нижняя часть тела заменена на ховер-платформу, что позволяет ему бесшумно парить в медицинских лабораториях. Торс человеческий, но с респиратором над ртом и ярко-оранжевыми светящимися глазами. Вокруг его тела расположены острые инструменты - пила вместо правой руки, механические конечности с иглами - изначально использовавшиеся для экспериментов, но вовлечённые в дело при нападении.

Загнанный в угол, Учёный бросает гранату с отравляющим газом, которая взрывается, выпуская токсичное облако, наносящее урон с течением времени и проходящее сквозь броню. Его атаки в ближнем бою пилой и иглами очень жестоки на близкой дистанции. Он часто крадётся из-за углов или парит в воздухе, чтобы застать врасплох злоумышленников в медицинских учреждениях Строггов.

СОВЕТЫ:

- Соблюдайт дистанцию ​​- его инструменты ближнего боя смертельно опасны вблизи.
- Избегай ядовитых облаков; они дезориентируют и проходят сквозь броню.
- Используй дальнобойное/автоматическое оружие, чтобы быстро уничтожить его.
- Будь внимателен к зависанию в воздухе; зависание на вертолёте затрудняет обнаружение по звуку.