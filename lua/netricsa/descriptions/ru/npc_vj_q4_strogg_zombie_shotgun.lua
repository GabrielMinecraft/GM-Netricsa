НЕУДАЧНЫЙ ЗОМБИ-ТРАНСФЕР СТРОГГОВ C СТРОГГОСА


КЛАСС:        Строгг, Зомби
МЕТАБОЛИЗМ:   Частично строггифицированный человек (гибрид плоти и мутации)
ВОСПРИЯТИЕ:   Базовое визуальное + обоняние / обнаружение
РАЗМЕР:       ~5,5 футов
ВЫНОСЛИВОСТЬ: Средне-низкая
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       Дробовик
НАГРАДА:      300
УГРОЗА:       Средняя

ОПИСАНИЕ:

Неудачные трансферы более мобильны (хотя в целом всё ещё медлительны), вооружены дробовиками и способны нанести значительный урон, если подпустить их слишком близко.

Неудачные трансферы также появляются в Нексусе во время основных сцен. Даже будучи разрубленными пополам, некоторые зомби продолжают представлять угрозу (части тела всё ещё могут атаковать).

СОВЕТЫ:

- Дробовики и оружие средней мощности эффективны против неудавшихся трансферов.
- Целься в голову или грудь - выстрелы в голову наносят дополнительный урон.