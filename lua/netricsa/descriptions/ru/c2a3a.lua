ЧЁРНАЯ МЕЗА, ЛОГОВО ИХТИОЗАВРА


Добро пожаловать в затопленный исследовательский бассейн. Сканеры подтверждают, что ты не один - глубоко под поверхностью движется что-то огромное.

Идентификация: Ихтиозавр, водный хищник с Зена.

Скорость: экстремальная.

Аппетит: бездонный.

Вероятность выживания без оружия: не будем это вычислять.

Над ватерлинией всё ещё держится небольшой ремонтный мостик. На нём учёный из Чёрной Мезы отчаянно машет рукой, оказавшись между затопленной лабораторией и кишащим водным монстром. Его шансы на выживание без твоего вмешательства... математически несоизмеримы.

Ихтиозавр кружит, словно акула, ожидая, когда ты нырнёшь. Каждый всплеск притягивает его ближе, каждая секунда под водой увеличивает риск стать его обедом. Использовать здесь стандартное оружие будет сложно - плохая видимость и ненадёжная отдача. Тебе придётся проявить изобретательность, если хочешь перехитрить его.