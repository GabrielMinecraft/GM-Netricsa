БРОНИРОВАННАЯ ТУРЕЛЬ ЧЁРНОЙ МЕЗЫ


КЛАСС:      Автоматизированная оборонительная единица
МЕТАБОЛИЗМ: нет (механическая)
ВОСПРИЯТИЕ: датчики движения, обнаружение звука
РАЗМЕР:       ~4 фута (стационарная)
ВЫНОСЛИВОСТЬ:  высокая (бронированный корпус)
ВРАЖДЕБНОСТЬ:  крайняя (автоматическое вступление в бой)
ОРУЖИЕ:    скорострельный тяжелый пулемет
НАГРАДА:     600
УГРОЗА:     высокая

ОПИСАНИЕ:

Бронированная турель Чёрной Мезы - это сильно усиленное автоматическое оборонительное устройство, предназначенное для охраны коридоров, лабораторий и важных объектов. В отличие от стандартных турелей, она может быть установлена как на земле, так и в перевернутом положении на потолке, что позволяет стрелять под непредсказуемыми углами.

Её броня обеспечивает защиту от стрелкового оружия, а скорострельный тяжелый пулемет наносит сокрушительный угнетающий огонь. Размещенная в узких местах или узких коридорах, турель может контролировать доступ с разрушительной эффективностью, делая засады и фронтальные атаки чрезвычайно опасными.

СОВЕТЫ:

- Цельтесь в слабые места конструкции; броня защищает большую часть передней части.
- Против нее эффективны взрывчатые вещества или тяжелая огневая мощь.
- Следите за турелями, установленными на потолке - всегда проверяйте верхнюю часть помещения.
- Избегайте пребывания на открытых пространствах; их поле обстрела смертельно опасно.