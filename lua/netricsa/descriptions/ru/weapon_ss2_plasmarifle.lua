ПЛАЗМЕННАЯ ВИНТОВКА XL-808


ОРУЖИЕ:  Плазменная Винтовка XL-808 
ПАТРОНЫ: Плазматические клетки X8
СС:      ~400 выстр/мин


ОПИСАНИЕ:

XL-808 - высокоэнергетическое плазменное оружие, предназначенное для подавления больших групп противников и удержания зон. Выпускает плотные плазменные залпы с высокой скорострельностью; снаряды обладают небольшой "липкостью" к органическим целям, что повышает вероятность попадания по движущимся противникам.


ПРИМЕЧАНИЯ:

- Идеально для зачистки скоплений слабых врагов и удержания проходов
- Требует контроля расхода боеприпасов.