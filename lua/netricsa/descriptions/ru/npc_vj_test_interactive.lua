ИНТЕРАКТИВНЫЙ ГРАЖДАНИН


КЛАСС:        Гражданский / Экспериментальный
МЕТАБОЛИЗМ:   Человек, Мужской
ВОСПРИЯТИЕ:   Нормальное (базовое восприятие)
РАЗМЕР:       ~1,7-1,8 м
ВЫНОСЛИВОСТЬ: Низкая (хрупкий, безоружный)
ВРАЖДЕБНОСТЬ: Очень низкая (не боец)
ОРУЖИЕ:       Нет
НАГРАДА:      18
УГРОЗА:       Минимальная

ОПИСАНИЕ:

Интерактивный гражданин - это странный экспериментальный гражданин, созданный для взаимодействия в режиме песочницы. В отличие от обычных граждан, этому варианту можно приказать убить вас, что добавляет мрачного юмора в игровой процесс.

СОВЕТЫ:

- Может использоваться для исследования от третьего лица или творческих сценариев.
- Активация его действия «убить себя» может добавить экспериментальный или юмористический элемент.