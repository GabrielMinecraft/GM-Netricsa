ОПИСАНИЕ:

Apache - это тяжело вооруженный военный вертолет, используемый H.E.C.U. для уничтожения важных целей и подавления сопротивления на открытых пространствах. Его композитная броня не пробивается стрельбой из стрелкового оружия, а маневренность в воздухе делает его трудноуязвимым даже для тяжелого оружия. Вооруженный скорострельным пулеметом, неуправляемыми ракетами и иногда самонаводящимися ракетами, он с легкостью может уничтожить как пехоту, так и транспортные средства.

Рёв лопастей ротора, сопровождаемый обстрелом - это смертный приговор для любого, кто оказался на открытой местности. Апачи обычно действуют в координации с наземными войсками, выполняя роль воздушных палачей, пока морские пехотинцы продвигаются вперед. Прямое столкновение без взрывчатки или энергетического оружия бесполезно.

СОВЕТЫ:

- Укрытие - ваш лучший союзник - никогда не сражайтесь с Апачем на открытой местности.
- Взрывчатые вещества (РПГ или установленное оружие) - единственное реальное противодействие.
- Самонаводящиеся ракеты смертельно опасны - двигайтесь непредсказуемо.
- Если вертолет сбит, держитесь подальше - обломки и вторичные взрывы могут быть смертельны.