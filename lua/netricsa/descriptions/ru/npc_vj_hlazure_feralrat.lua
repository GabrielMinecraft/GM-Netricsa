ДИКАЯ КРЫСА С ЗЕМЛИ


КЛАСС:        Грызун, опасный для окружающей среды
МЕТАБОЛИЗМ:   Млекопитающий
ВОСПРИЯТИЕ:   Зрительное + обоняние
РАЗМЕР:       ~30 см
ВЫНОСЛИВОСТЬ: Низкая-средняя
ВРАЖДЕБНОСТЬ: Средняя
ОРУЖИЕ:       Укус (умеренный), атака в прыжке
НАГРАДА:      10
УГРОЗА:       Низкая-средняя

ОПИСАНИЕ:

Дикие крысы - это агрессивные варианты обычных крыс, мутировавшие или подвергшиеся стрессу в условиях Каскадного Резонанса. Они прыгают, чтобы укусить цель, и нападают небольшими группами. Хотя по отдельности они слабы, стаи могут эффективно преследовать злоумышленников.

СОВЕТЫ:

- Соблюдайте дистанцию - следите за атаками в прыжке.
- Дробовики или атаки ближнего боя лучше всего работают в ограниченном пространстве.
- Опасны только в большом количестве.