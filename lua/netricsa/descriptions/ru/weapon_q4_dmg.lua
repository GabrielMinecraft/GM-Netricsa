ПУШКА ТЁМНОЙ МАТЕРИИ


ОРУЖИЕ:  Пушка Тёмной Материи
ПАТРОНЫ: Заряды Тёмной Материи
СС:      ~30 выстрелов/мин

ОПИСАНИЕ:

Пушка Тёмной Материи - экспериментальное оружие Строггов, использующее опасные сгустки тёмной материи как боеприпасы. При попадании снаряд создаёт мощную чёрную дыру, затягивающую врагов и наносящую непрерывный урон. Идеально подходит для подавления скоплений врагов и разрушения защиты.

ПРИМЕЧАНИЯ:

- Эффект "сжатия" - враги вокруг точки попадания притягиваются к центру.
- Риск нестабильности: может отбрасывать стрелка назад при мощных взрывах.
- Очень эффективен на средних дистанциях, особенно в узких коридорах.
- Заряды ограничены - каждый выстрел нужно рассчитывать тщательно.