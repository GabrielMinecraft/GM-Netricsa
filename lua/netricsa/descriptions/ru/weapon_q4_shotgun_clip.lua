ДРОБОВИК МОРПЕХОВ


ОРУЖИЕ:  Дробовик (Помповый)
ПАТРОНЫ: Патроны Дробовики
СС:      ~48 выстрелов/мин

ОПИСАНИЕ:

Классический дробовик ближнего боя. При каждом нажатии выстреливает залп дроби и требует помповой перезарядки перед следующим выстрелом. Первоначально имеет внутренний магазин на восемь патронов; позже появляется апгрейд, позволяющий использовать магазин на десять штук. Отличен против врагов в узких пространствах и ближних перестрелках. 


ПРИМЕЧАНИЯ:

- Урон до 140 при попадании всех дробей. 
- Максимальный боезапас до ~50 с запасными патронами. 
- Менее эффективен на средних и дальних дистанциях из-за разброса дроби. 
- Апгрейд с магазином на 10 уменьшает время перезарядки и позволяет реже промедлять.