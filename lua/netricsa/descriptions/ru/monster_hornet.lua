ХОРНЕТ С ЗЕНА


КЛАСС:      снарядный организм Зена
МЕТАБОЛИЗМ: Насекомоподобный инопланетянин (выращенный в улье)
ВОСПРИЯТИЕ: Инстинкт теплового наведения, простое наведение на цель
РАЗМЕР:       ~30 см
ВЫНОСЛИВОСТЬ:  Очень низкая (хрупкий, легко уничтожается в полёте)
ВРАЖДЕБНОСТЬ:  Высокая (бессмысленно атакует ближайшую цель)
ОРУЖИЕ:    Пронзительное жало (биологический дротик)
НАГРАДА:     5 
УГРОЗА:     Низкая (по отдельности), высокая (в роях)

ОПИСАНИЕ:

Шершни - это живые инопланетные снаряды, выращенные в ульях и запускаемые инопланетными пехотинцами из их органических ручных пушек. После запуска они быстро летят к цели, корректируя курс с тепловой точностью. Хотя по отдельности они слабы, их настойчивость и роевая природа делают их смертельно опасными в больших количествах.

Шершни растворяются после удара или при уничтожении в полете, не оставляя после себя никаких материалов, пригодных для восстановления. Их существование отражает биоинженерную войну Ксена, превращающую даже боеприпасы в живое оружие.

СОВЕТЫ:

- Не останавливайтесь - шершни легко отслеживают неподвижные цели.
- Стреляйте в них в полёте, чтобы уменьшить урон от роя.
- Используйте взрывчатые вещества или интенсивный огонь, чтобы проредить группы инопланетных солдат, прежде чем они выпустят слишком много шершней.
- У них нет иглы или красного платья.