КОМПЛЕКС ЛЯМБДА ЧЁРНОЙ МЕЗЫ, ПЕРЕСТРЕЛКА


Лифт со скрежетом опустился вниз, и в тот же миг, как двери с визгом распахнулись, ты оказался в полном хаосе. Солдаты H.E.C.U. всё ещё сражаются за жизнь, их последние отряды окопались за разрушенными баррикадами.

Пехота пришельцев вывела на поле боя тяжёлую добычу - громадную наземную лазерную пушку. Каждый выстрел прорезает бетон, словно бумагу, превращая в пепел как укрытия, так и людей.