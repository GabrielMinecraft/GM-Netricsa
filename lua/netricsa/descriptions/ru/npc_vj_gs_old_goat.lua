СТАРЫЙ КОЗЁЛ (ТИ-РЕКС)


КЛАСС:        Зверь
МЕТАБОЛИЗМ:   Хищник (но, как ни странно, питается добычей и людьми)
ВОСПРИЯТИЕ:   Ограниченное, но компенсируется чистой яростью
РАЗМЕР:       Гигантский (~9 метров)
ВЫНОСЛИВОСТЬ: Очень высокая
ВРАЖДЕБНОСТЬ: До смешного экстремальная
ОРУЖИЕ:       Огромные челюсти, удар хвостом, «Козий рык»
НАГРАДА:      9999 + сломанный квестовый предмет
УГРОЗА:       Абсурдно высокая

ОПИСАНИЕ:

Известный в хрониках MMO как «Старый козёл», этот доисторический мутант случайно появился внутри гигантского унитаза в средневековом мире RPG. Игроки поначалу думали, что это просто пасхальное яйцо, пока не выяснилось, что это существо может уничтожить целый рейд из 40 человек за считанные секунды.

Он выглядит как классический тираннозавр, а его стиль боя сочетает в себе ярость динозавра.

СОВЕТЫ:

Если увидите это на унитазе - бегите.