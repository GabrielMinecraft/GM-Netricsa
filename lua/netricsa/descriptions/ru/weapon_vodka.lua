СПИРТНАЯ ВОДКА


ОРУЖИЕ:      Спиртная Водка
РЯД:         Одна бутылка

ОПИСАНИЕ:

Мистический напиток, наполненный сырой энергией. После его употребления тело сразу же наполняется силой, хотя жжение алкоголя остаётся.

ПРИМЕЧАНИЯ:

- Увеличивает силу после употребления.
- Вызывает сильное опьянение.
- Лучше всего использовать в отчаянных ситуациях.