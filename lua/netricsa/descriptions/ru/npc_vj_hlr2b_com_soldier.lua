БЕТА, СОЛДАТ OVERWATCH АЛЬЯНСА 


КЛАСС:        Прототип трансчеловеческой пехоты
МЕТАБОЛИЗМ:   Аугментированный человек, мужской (ранняя модификация Альянса)
ВОСПРИЯТИЕ:   Человеческое зрение + рудиментарное тактическое программирование
РАЗМЕР:       180-180 см
ВЫНОСЛИВОСТЬ: Средняя
ВРАЖДЕБНОСТЬ: Высокая
ОРУЖИЕ:       XM29 OICW, Осколочные гранаты
НАГРАДА:      300
УГРОЗА:       Средняя

ОПИСАНИЕ:

Бета солдат Overwatch представляет собой ранний этап попыток Альянса создать надежную трансчеловеческую армию. В отличие от финальных отрядов Overwatch, появившихся позже, эти прототипы сохранили больше человеческих черт и использовали более громоздкое, менее продвинутое снаряжение. Их броня лоскутная, обеспечивает среднюю защиту, а их кибернетические аугментации примитивны по сравнению с более поздними поколениями.

Несмотря на то, что отряды Бета были менее дисциплинированными и более уязвимыми, чем современные солдаты Overwatch, они были развернуты в большом количестве на ранних этапах оккупации Альянсом. Они полагались на чистую агрессию и численное превосходство, а не на точную тактику, часто атакуя противника устаревшим оружием и плохой координацией.

Их существование предвещало безжалостную доработку конструкции транслюдей Альянса - несовершенных предшественников, проложивших путь для совершенных элитных сил.

СОВЕТЫ:

- Более слабая броня, чем у финальных отрядов Альянса - цель в центре масс.
- Менее точны на дальней дистанции, но смертоносны в ближнем бою.