КОЗЁЛ С ГОАТВИЛЛЯ


КЛАСС:        Млекопитающее, Парно-Копытное
МЕТАБОЛИЗМ:   Травоядное (но ведёт себя как воплощение хаоса)
ВОСПРИЯТИЕ:   Низкое, непредсказуемое
РАЗМЕР:       ~3 фута в холке
ВЫНОСЛИВОСТЬ: Высокая (иммунитет к большинству физических повреждений)
ВРАЖДЕБНОСТЬ: Хаотично-нейтральная
ОРУЖИЕ:       Удар головой, рэгдолльный хаос, лизание
НАГРАДА:      200
УГРОЗА:       Средняя

ОПИСАНИЕ:

Козёл - это козёл, бросающий вызов законам физики, который превращает любое окружение в чистый хаос. Он бросается головой вперёд на машины, предметы и людей, отправляя их в полёт с невероятной силой. Несмотря на то, что он всего лишь козёл, его способность становится рэгдоллом, лизать и тащить предметы, а также выживать во время взрывов делает его ходячей (и блеющей) катастрофой.

СОВЕТЫ:

- Не недооценивай его удар головой; Он может бросать даже тяжёлые предметы.
- Если он валяется, значит, он не умер, а просто нарушает законы физики.
- Хаос следует за ним, куда бы он ни пошёл; не стой рядом с хрупкими предметами.