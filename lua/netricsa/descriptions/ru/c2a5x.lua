ЧЁРНАЯ МЕЗА, ПЛОХОЕ КОМБО


Поздравляю, вселенная просто решила подкинуть тебе наихудшее из возможных сочетаний.

Ситуация: Тентакль крушит всё на своём пути во дворе каньона; Новый Апач кружит, обстреливая ракетами; орды Ментала вырываются из каждого обломка; хедкрабы заполонили низинное укрытие; а земля между тобой и целью - минное поле с неизвестно каким количеством детонаторов.