ЧЁРНАЯ МЕЗА, НЕУДАЧНЫЙ ПАРАЛЛЕЛЬНЫЙ ЭКСПЕРИМЕНТ


Ты прыгнул в их импровизированную версию Сирианского Ключа к Вечности. На мгновение это сработало - ты снова оказался внутри Зена, ощущая его невесомость, слыша его нескончаемый гул. Но прежде чем ты успел что-либо предпринять, вмешались вортигонты. Они втащили тебя обратно, вышвырнув из портала в Чёрную Мезу.

В то же время, в другом крыле, Антимасс-спектрометр рухнул под опекой "другого Сэма". Каскадный Резонанс пронёсся по всему комплексу, и твой эксперимент тоже разрушился. Прототип Ключа к вечности взорвался - нестабильный, сломанный, стёртый. Инопланетный пехотинец успел пройти до взрыва... но был разорван на части вместе с самой машиной.

Теперь всё рушится. Спектрометр уничтожен. Временной Замок уничтожен. Весь комплекс трещит по швам. Где-то там, другой ты уже на пути к Нихиланту.

Значит, у тебя есть узкое окно. Двигайся вперёд, пока можешь.

Хорошие новости: тебе не придётся спасать мир дважды. Плохие новости: тебе всё ещё придётся спасать себя.