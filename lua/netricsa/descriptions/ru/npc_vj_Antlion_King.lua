КОРОЛЬ МУРАВЬИНЫХ ЛЬВОВ


КЛАСС:        Муравьиный лев, Король
МЕТАБОЛИЗМ:   Насекомообразный
ВОСПРИЯТИЕ:   Осведомлённость Лорда Улья
РАЗМЕР:       Колоссальный (~9-18 м)
ВЫНОСЛИВОСТЬ: Чрезвычайно высокая
ВРАЖДЕБНОСТЬ: Чрезвычайная
ОРУЖИЕ:       Разящие Когти, Вызов Подкрепления
НАГРАДА:      100000
УГРОЗА:       Катастрофическая

ОПИСАНИЕ:

Король муравьиных львов был вершиной общества муравьиных львов, колоссальным лордом улья, призванным командовать вторжениями в гнёзда глубоко под землёй. Созданный как патриархальная фигура.
Он обитает в обширных подземных пещерах между Свалкой Элая Максвелла и Побережьем.

