ЧЁРНАЯ МЕЗА, КРЕПОСТЬ КАНЬОНА


Земля превратилась в систему каньонов - узкие хребты, неустойчивые скалы, и нигде не спрятаться. Идеальное место для охоты с чего-то воздушного.

Датчики подтверждают: новый боевой вертолёт "Апач" захватил цель. Не тот, что был раньше - свежая краска, новые ракеты и пилот, жаждущий сравнять счёт. Он кружит над краями каньона, используя стены как воронки, чтобы заманить тебя в зону поражения.