ИНЖЕНЕР ПОВСТАНЦЕВ


КЛАСС:        Техник Сопротивления людей
МЕТАБОЛИЗМ:   Люди
ВОСПРИЯТИЕ:   Высокое (тактическая осведомлённость + инженерные навыки)
РАЗМЕР:       ~1,7 м
ВЫНОСЛИВОСТЬ: Средняя (лёгкая броня, ловкость)
ВРАЖДЕБНОСТЬ: Высокая (к силам Альянса)
ОРУЖИЕ:       Заряженный лук, дробовик SPAS-12, развёртываемые турели Альянса
НАГРАДА:      350
УГРОЗА:       Средне-высокая

ОПИСАНИЕ:

Инженер повстанцев - важнейший юнит поддержки Сопротивления людей. Обученные как боевым, так и инженерным навыкам, они вооружены Заряженным луком для дальнобойных точных атак и могут развёртывать турели Альянса, перепрограммированные для защиты позиций повстанцев. Эти турели служат стационарной мощной огневой поддержкой, увеличивая боевую мощь небольших отрядов.

Инженеры-повстанцы ловки и находчивы, часто атакуют врагов с флангов, устанавливая турели или занимая стратегические позиции. Их технические навыки позволяют им взламывать и перенастраивать технику Альянса, что делает их незаменимыми в полевых операциях.

СОВЕТЫ:

- В первую очередь уничтожайте вражеских инженеров - они могут обратить оборону против вас.
- Защищайте установленные турели - они обеспечивают постоянную огневую поддержку.
- Используйте свою мобильность, чтобы размещать турели в неожиданных местах.
- «Заряженный лук» смертоносен на расстоянии и способен пробивать лёгкую броню.