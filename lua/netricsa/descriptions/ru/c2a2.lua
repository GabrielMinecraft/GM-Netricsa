ЧЁРНАЯ МЕЗА, ГРУЗОВАЯ ЖЕЛЕЗНАЯ ДОРОГА


Электричество снова в сети, и грузовая железнодорожная система снова работает. Вопреки всем трудностям, тебе удалось выбраться из этой смертельной ловушки. Хорошая новость: поезд работает. Плохая новость: всё остальное.

Как только ты забираешься в вагон и нажимаешь на рычаги управления, вагон рывком трогается с места, скрипя ржавыми колёсами. Пути уходят глубоко в подземный лабиринт Чёрной Мезы, обещая как возможность побега, так и новые кошмары.

Впереди сложности:

Патрули H.E.C.U. укрепили участки железной дороги. Ожидай засад в узких местах, турелей у туннелей и взрывчатки, заложенной на путях.

Активность инопланетян растёт - представители фауны Зена появляются прямо на путях, либо слишком глупые, чтобы двигаться, либо достаточно умные, чтобы преградить вам путь.