ЧЁРНАЯ МЕЗА, МАНТА


Как будто и без того было недостаточно хаоса, пустынное небо просто обязано было добавить изюминку. Скат-манта пролетел над головой, высадил отряд пехоты Зена и исчез, прежде чем ты успел как следует зафиксировать его. Теперь крыши кишат инопланетянами, и время от времени из теней вылезает паукообразное существо - ревностные воины "священного ордена", принявшие на себя служение Менталу по причинам, которые могла объяснить только теология.

Земля? Забудь. Двор заминирован, но тот, кто заложил заряды, был в игривом настроении - никаких схем, никаких меток, лишь невидимая смерть, поджидающая под ногами.

И как раз когда ты думаешь, что изучил все угрозы, в верхних окнах замечается движение. Одна рама чернее остальных, и внутри этой пустоты сидит что-то неподвижное, тяжёлое и неправильное: Каменный Снайпер. Он ещё не стрелял, но его взгляд настолько силён, что даже опытные солдаты замешкаются.

Обзор угрозы:

Высадка скат-манты: Войска Зена теперь патрулируют территорию.

Арахноиды: атакуют с крыш и стен, религиозный пыл заставляет их сражаться до последнего вздоха.

Минное поле: невидимо, рандомизированное расположение, гарантированная паника при спешке.

Каменный Снайпер: окопался в тёмном окне, прямой обзор на всём подходе. Ожидай сокрушительной точности, когда решит нанести удар.

Тактические советы:

Крыши - зоны поражения - очисти их, прежде чем идти на минное поле.

Используй обломки, гранаты или даже трупы, чтобы проверить заминированные пути. Всё лучше, чем твои красные ботинки.