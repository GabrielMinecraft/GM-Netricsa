ЧЁРНАЯ МЕЗА, ПОДГОТОВКА К ВЫХОДУ НА ПОВЕРХНОСТЬ


Учёные настаивают, что прямого пути на поверхность нет. Единственный путь - через них, но сначала тебе нужно добраться до их позиции.

Они заперлись на первом этаже за дверями. Единственный способ присоединиться к ним - активировать экспериментальные генераторы луча в этом секторе, достаточно мощные, чтобы пробить дыру в обрушившейся стене. Процесс нестабилен, и они не могут рисковать, выходить наружу, поэтому задача ложится на тебя.

Данные разведки:

Цель: добраться до второго этажа, активировать генераторы луча и расчистить путь учёным.

Награда: доступ к запрещённым технологиям. Тебе разрешили забрать прототип тау-пушки, хранящийся неподалёку. Обращайся осторожно - это экспериментальная, нестабильная и очень эффективная штука.

Цель: добраться до верхнего этажа живым, выпустить луч смерти, разрушающий стены, и соединиться с учёными. Только тогда путь к выходу на поверхность станет реальностью.