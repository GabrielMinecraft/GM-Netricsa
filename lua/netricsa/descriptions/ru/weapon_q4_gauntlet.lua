ПЕРЧАТКА


ОРУЖИЕ:  Перчатка
ПАТРОНЫ: Нет (ближний бой)
СС:      150 разрезов/мин 

ОПИСАНИЕ:

Перчатка - это оружие ближнего боя, похожее на цепную пилу.

При контакте наносит удар разреза и толчок (отбрасывание), отбрасывая противника назад после попадания.

ПРИМЕЧАНИЯ:

- Наносит 50 урона за один разрез. 
- Работает без боеприпасов - всегда доступна. 
- В порядке тактики: скрытные подходы, засады, быстрое сближение - ключевые приёмы. 
- Комбинация с Квадратным уроном позволяет убивать врагов с полным здоровьем одним разрезом.