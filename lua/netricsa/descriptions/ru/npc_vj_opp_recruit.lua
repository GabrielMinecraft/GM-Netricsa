ОПИСАНИЕ:

Рекрут H.E.C.U. представляет собой необученного солдата на ранних этапах обучения в учебном лагере. В отличие от своих полностью вооружённых коллег, этим рекрутам не хватает оружия, брони или боеготовности. Столкнувшись с опасностью, они паникуют и бегут вместо того, чтобы сражаться, часто пытаясь найти укрытие или следовать за командирами.

Они символизируют сырой материал Боевого подразделения по работе в опасных условиях - молодых, неопытных солдат, ещё не закалённых в реальных боях. Хотя по отдельности они безвредны, их присутствие подчёркивает обширную систему военной подготовки, которая подготовила закалённых в боях бойцов H.E.C.U..

Морпехи отправлены в Чёрную Мезу.

