МОЛНИЕСТРЕЛ


ОРУЖИЕ:  Молниестрел
ПАТРОНЫ: Энергетические заряды
СС:      ~550 выстр./мин

ОПИСАНИЕ:

Молниестрел - оружие дальнего боя, выпускающее последовательные молнии, которые проходят через врагов, нанося непрерывный урон. Прекрасно подходит для ведения огня по вытянутым линиям сопротивления. Пулевой эффект превращается в электрическую дугу, поражающую всё, что находится по линии огня.

ПРИМЕЧАНИЯ:

- Пробивает сквозь несколько целей, если они расположены в линию.
- Эффективность снижается при перекрытии укрытиями и при преградах.
- Требует контроля энергии - использование на пустом пространстве быстро истощает заряд.
- Лучше использовать так: заранее занять позицию и установить прицел на минимальное движение врагов.