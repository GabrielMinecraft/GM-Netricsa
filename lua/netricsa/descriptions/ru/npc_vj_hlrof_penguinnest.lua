ГНЕЗДО-СНЕГОВИК ПИНГВИНОВ


КЛАСС:        Улей
МЕТАБОЛИЗМ:   органический кластер
ВОСПРИЯТИЕ:   пассивное, если не атакован
РАЗМЕР:       1,8 м (6 футов)
ВЫНОСЛИВОСТЬ: средняя
ВРАЖДЕБНОСТЬ: отсутствует, пока не спровоцирован
ОРУЖИЕ:       порождает вооружённых пингвинов после смерти
НАГРАДА:      300
УГРОЗА:       средне-высокая (после смерти)

ОПИСАНИЕ:

Это огромное существо, похожее на снеговика - странный улей. Оно медленно передвигается и безвредно, пока его не потревожат. Снеговик носит цилиндр и держит небольшую табличку с изображением пингвина и предупреждением: «Остерегайтесь Пингвинов!» Внутри своего ледяного тела он таит множество пингвинов, к каждому из которых привязана граната. Когда Снеговика убивают, пингвины вырываются из его тела, атакуя всех поблизости, вызывая взрывной хаос. Его необычный внешний вид и скрытый смертоносный груз делают его уникальной угрозой.

СОВЕТЫ:

- Не убивайте Снеговика, если не готовы к граду гранат.
- Наблюдайте с безопасного расстояния; он не представляет угрозы, если его не трогать.
- Используйте дальнобойное оружие, чтобы уничтожить его, минимизировав воздействие на пингвинов.