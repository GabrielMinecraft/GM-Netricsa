ОПИСАНИЕ:

Робот Ассасин Чёрных Оперативникков - экспериментальный механизированный солдат, сочетающий в себе человеческие боевые навыки и роботизированную выносливость. В отличие от обычных Чёрных Оперативников, эти машины разработаны для полного отсутствия колебаний, страха и абсолютной преданности условиям миссии.

Их элегантный бронированный экзоскелет имитирует силуэт человека-Ассасина, но скрывает продвинутые системы наведения и синтетические мышечные волокна. Они двигаются с поразительной точностью, сочетая скрытность своих человеческих предшественников с неутомимой эффективностью машин.

Ходят слухи, что эти отряды были созданы в рамках теневых проектов, призванных заменить одноразовых оперативников. Скрыт ли человеческий разум внутри их металлических оболочек, остаётся неизвестным...

СОВЕТЫ:

- Относитесь к ним и как к солдатам, и как к машинам - они умны, но при этом надёжно бронированы.
- Обычные пули их почти не замедляют; лучше всего подходят взрывчатка или энергетическое оружие.
- Никогда не недооценивайте их ловкость - они могут бегать, как их человеческие аналоги.