ОПИСАНИЕ:

Рабочие муравьи-львы - это промышленные мастера и защитники колонии. Эти слепые насекомые, родом с Зена, используют кислотную слюну, чтобы вырывать сложные туннели в скалистой местности, поддерживая инфраструктуру улья и защищая камеры с личинками от вторжений. Хотя они не так агрессивны, как солдаты или стражи, они могут стать грозными, когда защищают своих личинок, особенно в тесных подземных пространствах.

