ЧЁРНАЯ МЕЗА, КОМПАКТОР МУСОРА


Сэм, твои «союзники» из H.E.C.U. только что выдали тебе билет первого класса прямо в мусоропровод.

Эта камера была спроектирована для уничтожения промышленного мусора, но сегодня её используют для тебя. Гидравлические дробилки бьют с такой силой, что могут превратить танки в блины, а шлифовальные ленты не щадят ничего, что попало между ними. Даже биомеханоиды Ментала превратились бы в металлолом за считанные секунды.

Телеметрия не показывает прямых выходов оттуда - это означает: они ожидали твоей смерти. Но сканеры фиксируют ремонтные шахты, непрочные решётки и коммуникационные трубы. Другими словами, выход есть, но тебе понадобятся скорость, расчёт времени и, возможно, чудо. Запрыгивай на контейнеры.

Рекомендация: будь начеку, избегай дробилок и ищи нестабильные конструкции, которые можно использовать. H.E.C.U. считают, что ты выпал из игры. Докажи им, что они ошибаются. Снова.