ЯДОВИТЫЙ ХЕДКРАБ С ЗЕНА


Класс:        Хедкраб, Ядовитая разновидность
Обмен         веществ: паразит
Восприятие:   обострённое обоняние и тепловое обнаружение
Размер:       1,7 фута
Выносливость: низкая
Враждебность: экстремальная
Оружие:       ядовитый укус, прыжок
Награда:      200
Угроза:       высокая

Описание:

Ещё одна ужасающая мутация с Зена, ядовитые хедкрабы, отличаются раздутыми чёрными телами. Их клыки выделяют яд, который практически мгновенно убивает жертву, превращая её в крошечный кусочек жизни. Действие яда постепенно ослабевает, но редко раньше, чем это сделают другие враги. Эти существа часто сопровождают ядовитых зомби, цепляясь за своего хозяина, пока их не бросят в бой, словно живое оружие.

Советы:

— Их яд - самый смертоносный из всех видов хедкрабов.
— Сражайтесь на расстоянии - не рискуйте вступать в ближний бой.