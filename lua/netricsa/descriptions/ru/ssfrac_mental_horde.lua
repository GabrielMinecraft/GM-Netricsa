ОРДА МЕНТАЛА


Орда Ментала (также известная как Империя Тах-Ума или Враги Осириса) - это безжалостная армия бесчисленных инопланетных рас, объединённых волей их повелителя - Ментала, последнего из древней расы Хум-Тах. Цель Орды - полное уничтожение всей разумной жизни во Вселенной, не служащей своему богу.

Ментал собирает свои легионы из рабов, наёмников, мутантов и существ, созданных с помощью магии и технологий. Он подчиняет целые цивилизации, обещая силу или обращая их силой в своих безвольных слуг. Некоторые видят в нём божество, другие - проклятие галактики, но все без исключения либо служат ему, либо гибнут.

Орда Ментала не знает тактики или жалости - её сила в безграничном количестве и фанатичном повиновении. Под её натиском падали империи, исчезали народы и гасли звёзды. Когда Ментал пробуждается, начинается новый цикл геноцида, и миры один за другим превращаются в прах.

Ментал использует как технологии, так и тёмную магию: он создаёт киборгов, оживляет мёртвых, управляет порталами и целыми армиями чудовищ - от арахноидов и рептилоидов до безголовых солдат и демонов.

Там, где проходит Орда Ментала, не остаётся ничего - лишь тишина и руины.